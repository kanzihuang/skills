def main() -> None:
    args = parse_args()

    # Read input
    with open(args.input_file, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Basic validation
    if not data.get("book_title"):
        print("Error: missing 'book_title' in input", file=sys.stderr)
        sys.exit(1)
    if not data.get("book_id") and not data.get("suffix"):
        print("Error: missing 'book_id' or 'suffix' in input", file=sys.stderr)
        sys.exit(1)
    if not data.get("words"):
        print("Error: 'words' array is empty", file=sys.stderr)
        sys.exit(1)

    def _better_sentence(old: dict, new: dict) -> bool:
        """Return True if *new* entry has a better sentence than *old*.

        Same logic as match_sentences._better():
          Tier 0: non-fragment beats fragment (is_fragment=False > True)
          Otherwise: shorter wins above MIN_SENTENCE_LENGTH (30);
                     longer wins below it.
          Tie (same length): keep old.
        """
        # Tier 0 — completeness: non-fragment beats fragment
        old_frag = old.get('is_fragment', False)
        new_frag = new.get('is_fragment', False)
        if old_frag != new_frag:
            return not new_frag  # pick new only if it's complete

        la = len(old.get('sentence', ''))
        lb = len(new.get('sentence', ''))
        if la == lb:
            return False  # tie → keep old
        if (la < MIN_SENTENCE_LENGTH or lb < MIN_SENTENCE_LENGTH) ^ (la < lb):
            return False  # keep old
        return True  # pick new

    # Deduplicate by (lemma, pos) — not by surface word form.
    # WordId = {lemma}_{pos}_{suffix}, POS is included precisely so that
    # the same lemma with different POS (e.g. astray ADJ vs ADV,
    # virgin ADJ vs NOUN) produces separate cards.
    # When collisions occur, _better_sentence() keeps the entry with
    # the better sentence (shorter in sweet-spot, non-fragment beats
    # fragment), matching match_sentences.py's sentence selection.
    seen: dict[tuple[str, str], dict] = {}
    deduped = []
    dedup_details: list[str] = []
    for w in data["words"]:
        key = (w["lemma"].strip().lower(), w["pos"].strip().upper())
        if key not in seen:
            seen[key] = w
        else:
            existing = seen[key]
            if _better_sentence(existing, w):
                dedup_details.append(
                    f"    {existing.get('word', '?')} "
                    f"→ replaced by {w.get('word', '?')} "
                    f"(better sentence: "
                    f"{len(existing.get('sentence',''))} → {len(w.get('sentence',''))} chars)"
                )
                seen[key] = w
            else:
                dedup_details.append(
                    f"    {w.get('word', '?')} "
                    f"(lemma={w.get('lemma', '?')}, "
                    f"pos={w.get('pos', '?')}) — dropped, kept {existing.get('word', '?')}"
                )
    deduped = list(seen.values())
    if len(deduped) < len(data["words"]):
        dropped = len(data["words"]) - len(deduped)
        print(f"\n[DEDUP] {dropped} duplicate (lemma, pos) entries "
              f"dropped from batch:")
        for detail in dedup_details:
            print(detail, file=sys.stderr)
        print(
            "  This may indicate Step 2F POS fix collisions. "
            "Review the dropped entries and adjust POS if needed.\n",
            file=sys.stderr,
        )
    data["words"] = deduped

    # Verify no two entries share the same audio filename prefix.
    # The prefix is {safe_filename(lemma)}_{pos} — same prefix means
    # both word and sentence audio files would collide.
    # This is a defense-in-depth check: the (lemma,pos) dedup above
    # should prevent this, but a bug in dedup logic could silently
    # produce audio collisions (see commit 64c0b18).
    audio_keys: dict[str, list[str]] = {}
    for w in data["words"]:
        lemma = w.get("lemma", "").strip() or w["word"]
        pos = w.get("pos", "").strip()
        safe = safe_filename(lemma)
        prefix = f"{safe}_{pos}" if pos else safe
        if prefix not in audio_keys:
            audio_keys[prefix] = []
        audio_keys[prefix].append(w["word"])
    collisions = {k: v for k, v in audio_keys.items() if len(v) > 1}
    if collisions:
        print("Error: audio filename collision detected:", file=sys.stderr)
        for prefix, words in collisions.items():
            print(f"  prefix '{prefix}_*': {len(words)} words — {', '.join(words)}",
                  file=sys.stderr)
        print("  Each (lemma,pos) must have a unique audio prefix. "
              "Check dedup logic.", file=sys.stderr)
        sys.exit(1)

    # Pre-fill IPA from cmudict before validation.
    # Claude may leave ipa empty — cmudict fills it automatically for known words.
    # Only words truly not in cmudict will still have empty ipa and fail validation
    # (requiring Claude to provide a manual IPA).
    for w in data["words"]:
        if not w.get("ipa", "").strip():
            json_lemma = w.get("lemma", "").strip()
            lookup_word = json_lemma if json_lemma else w["word"]
            cmu_ipa = _cmu_ipa(lookup_word)
            if cmu_ipa:
                w["ipa"] = cmu_ipa

    # Validate quality rules before sync — only entries that will be
    # processed (skip _already_in_anki entries whose fields were not re-generated)
    active_words = [w for w in data["words"] if not w.get("_already_in_anki")]
    errors = validate_word_entries(active_words)
    if errors:
        print(f"Quality check FAILED ({len(errors)} error(s)):", file=sys.stderr)
        for e in errors:
            print(f"  {e}", file=sys.stderr)
        sys.exit(1)

    # Deck name resolution priority:
    #   1. Anki existing deck by bookId (sync() auto-corrects via
    #      find_deck_for_book_id — prevents accent/spelling drift)
    #   2. JSON deck_name field (set by Claude from Step 0b
    #      {deck: bookId} mapping via cardsInfo API)
    #   3. --deck CLI flag (explicit override)
    #   4. Auto-derive from book_title + book_author (new deck, fallback)
    #      vocab-book mode (suffix w/o book_id) appends " - 分级词汇"
    deck_name = _derive_deck_name(data, args.deck)

    try:
        result = sync(
            data,
            deck_name,
            dry_run=args.dry_run,
            no_audio=args.no_audio,
            verbose=args.verbose,
            word_timeout=args.word_timeout,
            prefetch=args.prefetch,
            audio_dir=args.audio_dir,
            no_direct_media=args.no_direct_media,
            no_ankiweb_sync=args.no_ankiweb_sync,
        )
        if result.get("error"):
            sys.exit(1)
    except AnkiConnectError as e:
        print(f"\nError: {e}", file=sys.stderr)
        print(
            "\nMake sure:\n"
            "  1. Anki is running\n"
            '  2. AnkiConnect plugin is installed (add-on code: 2055492159)\n'
            "  3. The Vocabulary Card (WeRead) model exists in Anki\n"
            "     (import an .apkg file from this skill first)",
            file=sys.stderr,
        )
        sys.exit(1)


