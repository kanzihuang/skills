def _compute_level_word_counts(words: list[dict]) -> dict[int, int]:
    """Count new words per COCA level from the input word list."""
    counts: dict[int, int] = {}
    for w in words:
        lvl = w.get("coca_level")
        if isinstance(lvl, int) and 1 <= lvl <= 25:
            counts[lvl] = counts.get(lvl, 0) + 1
    return counts


def _get_target_level_for_word(w: dict) -> int | None:
    """Get the target COCA level for a word (1-25 or None)."""
    lvl = w.get("coca_level")
    if isinstance(lvl, int) and 1 <= lvl <= 25:
        return lvl
    return None


def _compute_final_level_counts(
    words: list[dict],
    existing_map: dict[str, tuple[int, str]],
    namespace_id: str,
    bands: list[dict],
) -> dict[int, int]:
    """Compute final card count per COCA level after dedup.

    For each word: if WordId already exists → count at its current level
    (parsed from CocaLevel or deck name); if new → count at the
    word's target level.
    """
    # Build level → band mapping
    level_to_band: dict[int, tuple[int, int]] = {}
    for b in bands:
        for lvl in range(b["lo"], b["hi"] + 1):
            level_to_band[lvl] = (b["lo"], b["hi"])

    # Start with existing counts per level from the batch
    counts: dict[int, int] = {}
    for w in words:
        wid = _make_word_id(w, namespace_id)
        if wid in existing_map:
            lvl = w.get("coca_level")
            if isinstance(lvl, int) and 1 <= lvl <= 25:
                counts[lvl] = counts.get(lvl, 0) + 1
        else:
            lvl = _get_target_level_for_word(w)
            if lvl is not None:
                counts[lvl] = counts.get(lvl, 0) + 1
    return counts


def _build_subdeck_name(parent_deck: str, band: dict) -> str:
    """Build a full sub-deck path.

    e.g. "My Book - 分级词汇::COCA 3-5"
    """
    return f"{parent_deck}::{band['name']}"


def _print_deck_plan(
    data: dict,
    deck_name: str,
    new_word_counts: dict[int, int],
    bands: list[dict],
) -> None:
    """Print the deck plan — sub-deck names and word counts."""
    print(f"\n  Deck plan for \"{deck_name}\":")
    for band in bands:
        lo, hi = band["lo"], band["hi"]
        level_new = sum(
            c for lvl, c in new_word_counts.items() if lo <= lvl <= hi
        )
        print(f"    {band['name']}: {level_new} new words")
    print()


def _migrate_misplaced_cards(
    ac: "AnkiConnect",
    parent_deck: str,
    bands: list[dict],
    existing_map: dict | None = None,
) -> int:
    """Migrate ALL cards under parent to correct COCA sub-deck.

    Reads CocaLevel field from each card, determines target sub-deck,
    and moves cards that are in the wrong deck.  Returns count of
    migrated cards.
    """
    if existing_map is not None:
        existing = existing_map
    else:
        existing = ac.get_word_id_map_with_deck(parent_deck)
    if not existing:
        return 0

    # Build level → target sub-deck mapping (without count in name)
    level_to_target: dict[str, str] = {}  # coca_level_str → target deck prefix
    for band in bands:
        for lvl in range(band["lo"], band["hi"] + 1):
            prefix = f"{parent_deck}::{band['name']}"
            level_to_target[str(lvl)] = prefix

    # Get full note info to read CocaLevel
    all_note_ids = list({nid for nid, _deck in existing.values()})
    if not all_note_ids:
        return 0

    # Fetch CocaLevel from notesInfo
    info = ac.notes_info(all_note_ids)
    note_coca: dict[int, str] = {}
    for n in info:
        coca = (n.get("fields", {})
                 .get("CocaLevel", {})
                 .get("value", ""))
        note_coca[n["noteId"]] = coca

    migrated = 0
    for word_id, (note_id, current_deck) in existing.items():
        coca_str = note_coca.get(note_id, "")
        if not coca_str:
            continue
        target_prefix = level_to_target.get(coca_str)
        if not target_prefix:
            continue
        # Check if current deck exactly matches target deck
        if current_deck == target_prefix:
            continue  # Already in correct deck

        # Migrate: move cards to correct sub-deck
        try:
            card_ids = ac.get_cards_of_notes([note_id])
            if card_ids:
                ac.change_deck(card_ids, target_prefix)
                migrated += 1
        except AnkiConnectError:
            pass

    return migrated


def _make_word_id(word_data: dict, book_id: str,
                   resolved_lemma: str | None = None) -> str:
    """Compute the canonical WordId for a word entry.

    WordId = {safe_filename(resolved_lemma)}_{pos}_{book_id}

    POS is included to prevent collisions when the same lemma appears
    with different parts of speech (e.g. "walk" as NOUN vs VERB).
    """
    if resolved_lemma:
        resolved = resolved_lemma
    else:
        resolved = lemmatize(
            word_data["word"],
            json_lemma=word_data.get("lemma", ""),
        )
    pos = word_data.get("pos", "").strip()
    if pos:
        return f"{safe_filename(resolved)}_{pos}_{book_id}"
    return f"{safe_filename(resolved)}_{book_id}"


def _process_one_word(
    w: dict,
    book_id: str,
    no_audio: bool,
    word_timeout: int = WORD_TIMEOUT,
) -> tuple[dict, list[tuple[str, bytes]], str]:
    """Generate audio and build a note for a single word.

    Runs in a thread so it can be subject to a per-word timeout.

    Returns (note, audio_uploads, ipa).
    """
    word = w["word"]
    # Trust the lemma from match_sentences.py (authoritative, per-sentence spaCy analysis).
    # Falls back to the word itself if lemma is missing (degraded mode).
    lemma = w.get("lemma", "").strip() or word
    pos = w.get("pos", "").strip()
    pos_part = f"_{pos}" if pos else ""

    safe = safe_filename(lemma)

    # IPA: cmudict preferred.  Claude's IPA used as tiebreaker for
    # heteronyms (read/read, wound/wound) and fallback for OOV words.
    cmu_ipa = _cmu_ipa(lemma, w.get("ipa", ""))
    ipa = cmu_ipa if cmu_ipa else w.get("ipa", "")

    audio_uploads: list[tuple[str, bytes]] = []

    if not no_audio:
        # Word audio: Edge TTS on lemma (with IPA if available).
        #   Include book_id + POS — same lemma different POS (e.g.
        #   astray ADJ vs ADV) must not share audio filenames.
        tts_timeout = max(word_timeout // 2, 10)
        if ipa:
            tts = edge_tts_bytes(lemma, ipa=ipa, timeout=tts_timeout)
            if tts:
                audio_uploads.append((f"{safe}{pos_part}_{book_id}_word.mp3", tts))
            else:
                raise RuntimeError(
                    f"Word audio generation failed for '{lemma}' "
                    f"(TTS returned no data after {EDGE_TTS_MAX_RETRIES + 1} attempts)"
                )

        # Sentence audio: Edge TTS on cleaned sentence.
        #   Include book_id + POS in filename — different POS have
        #   different sentences and must not collide.
        clean = re.sub(r"<[^>]+>", "", w["sentence"])
        sent_tts = edge_tts_bytes(clean, timeout=tts_timeout)
        if sent_tts:
            audio_uploads.append((f"{safe}{pos_part}_{book_id}_sent.mp3", sent_tts))
        else:
            raise RuntimeError(
                f"Sentence audio generation failed for '{word}' "
                f"(TTS returned no data after {EDGE_TTS_MAX_RETRIES + 1} attempts)"
            )

    note = build_note_entry(w, ipa, book_id, lemma=lemma)
    # Preserve original index in new_words for correct band assignment.
    orig_idx = w.get("_idx")
    if orig_idx is not None:
        note["_idx"] = orig_idx
    return note, audio_uploads, ipa


def build_note_entry(
    word_data: dict,
    ipa: str | None,
    book_id: str,
    lemma: str | None = None,
) -> dict:
    """Build a single AnkiConnect note dict from word data.

    Audio files are referenced as [sound:filename.mp3] — the actual
    media files must be uploaded separately via store_media_file().

    If lemma is provided, it's used for the card Word field, audio
    filenames, and WordId.  WordId uses the resolved lemma for
    cross-book dedup consistency (see _make_word_id).
    """
    # WordId uses the resolved lemma — variants like "pondered" and
    # "pondering" map to the same WordId "ponder_{book_id}".
    # Use _make_word_id (single source of truth) so build_note_entry
    # and the dedup check always compute the same WordId.
    surface = word_data["word"].strip().lower()
    safe_lemma = safe_filename(lemma) if lemma else safe_filename(surface)
    word_id = _make_word_id(word_data, book_id, resolved_lemma=lemma)
    pos = word_data.get("pos", "").strip()
    pos_part = f"_{pos}" if pos else ""
    word_audio_ref = f"[sound:{safe_lemma}{pos_part}_{book_id}_word.mp3]"
    sent_audio_ref = f"[sound:{safe_lemma}{pos_part}_{book_id}_sent.mp3]"

    # Insert <b> tags around the target word using target_offset.
    # Sentence is stored without tags — we add them here for Anki display.
    # Use the original-casing word (not lowercased surface) for the tag text.
    sent = word_data.get("sentence", "")
    target_offset = word_data.get("target_offset", -1)
    display_word = word_data["word"]  # original casing from sentence
    if target_offset >= 0 and target_offset + len(display_word) <= len(sent):
        sent = (
            sent[:target_offset]
            + f"<b>{display_word}</b>"
            + sent[target_offset + len(display_word):]
        )

    coca_level = str(word_data.get("coca_level", ""))
    return {
        "deckName": "",  # filled in later by caller
        "modelName": MODEL_NAME,
        "fields": {
            "WordId": word_id,
            "Word": lemma if lemma else surface,
            "Sentence": sent,
            "IPA": ipa or word_data.get("ipa", ""),
            "DefinitionCN": word_data["definition_cn"],
            "TranslationCN": word_data["translation_cn"],
            "WordAudio": word_audio_ref,
            "SentenceAudio": sent_audio_ref,
            "CocaLevel": coca_level,
        },
        "tags": ["weread"],
    }


