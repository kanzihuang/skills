def sync(
    data: dict,
    deck_name: str,
    dry_run: bool = False,
    no_audio: bool = False,
    verbose: bool = False,
    word_timeout: int = WORD_TIMEOUT,
    prefetch: bool = False,
    audio_dir: str | None = None,
    no_direct_media: bool = False,
    no_ankiweb_sync: bool = False,
) -> dict:
    """Run the full sync workflow. Returns a summary dict."""
    words = data["words"]
    book_title = data["book_title"]

    # 命名空间标识，用于 WordId / 音频文件名隔离
    #   vocab-anki: 微信读书 bookId（如 "22720170"）
    #   vocab-book: filter_fulltext.py 生成的 UUID suffix（如 "a1b2c3d4e5f6"）
    namespace_id = data.get("book_id", "") or data.get("suffix", "")

    # Read bands from JSON (set by filter_fulltext.py)
    bands: list[dict] = data.get("bands", [])

    # 1. Connect to Anki (skip for prefetch — audio-only mode)
    ac = None
    existing_map: dict[str, tuple[int, str]] = {}

    if not prefetch:
        print('Connecting to AnkiConnect...')
        ac = AnkiConnect()
        # Pre-flight: fail fast before any heavy work (audio, deck creation)
        try:
            ac.version()
        except AnkiConnectError as e:
            print(f"FATAL: {e}", file=sys.stderr)
            sys.exit(1)

        # Auto-correct deck name by bookId
        if namespace_id:
            found_deck, count = ac.find_deck_for_book_id(namespace_id)
            if found_deck and found_deck != deck_name:
                resolved = found_deck
                if "::" in resolved:
                    resolved = resolved.split("::")[0]
                if data.get("book_id") and not data.get("suffix"):
                    if resolved.endswith(" - 分级词汇"):
                        resolved = resolved[:-len(" - 分级词汇")]
                if resolved != deck_name:
                    print(f'  Note: using existing deck "{resolved}" '
                          f'(found {count} cards with bookId={namespace_id})')
                    deck_name = resolved

        # 2. Create parent deck
        if not dry_run:
            ac.ensure_deck_and_model(deck_name, MODEL_NAME)
        print(f'  Deck: "{deck_name}"')

        # 3. Parent-level dedup: query all cards across all sub-decks
        if bands:
            print("\nQuerying existing cards across all sub-decks...")
            existing_map = ac.get_word_id_map_with_deck(deck_name)
            print(f"  Found {len(existing_map)} existing cards in parent deck")
        else:
            # No bands → flat deck
            existing_word_ids = ac.get_word_id_map(deck_name)
            existing_map = {
                wid: (nid, deck_name)
                for wid, nid in existing_word_ids.items()
            }
            print(f"  Found {len(existing_map)} existing cards")
    else:
        existing_map = {}  # prefetch: skip live Anki query; _already_in_anki markers handle filtering

    # 4. Compute new word counts per level and build target deck mapping
    level_new_counts = _compute_level_word_counts(words)
    word_target_deck: dict[int, str] = {}  # idx in new_words → target deck

    # Build level→band index for assignment
    level_to_band: dict[int, dict] = {}
    for band in bands:
        for lvl in range(band["lo"], band["hi"] + 1):
            level_to_band[lvl] = band

    # 5. Identify new words + migration candidates (parent-level dedup)
    new_words = []
    skipped_words = []
    seen_word_ids: set[str] = set()

    for idx, w in enumerate(words):
        word_id = _make_word_id(w, namespace_id)

        # Intra-batch dedup
        if word_id in seen_word_ids:
            if verbose:
                print(f"  SKIP {w['word']} (duplicate lemma in batch → {word_id})")
            skipped_words.append(w)
            continue
        seen_word_ids.add(word_id)

        # Pre-dedup: Step 2A-post marks words already known to be in Anki.
        # (sentence, word) key is stable across POS/lemma changes.
        # Applies in both prefetch and sync modes — in prefetch mode this
        # avoids generating audio for words that already have cards.
        if w.get('_already_in_anki'):
            if verbose:
                print(f"  SKIP {w['word']} (already in Anki, from Step 2A-post dedup)")
            skipped_words.append(w)
            continue

        # Parent-level dedup: check across all sub-decks
        if word_id in existing_map:
            if verbose:
                print(f"  SKIP {w['word']} (already in deck)")
            skipped_words.append(w)
        else:
            new_words.append(w)

    # Assign target deck for each new word.
    # Also build a WordId→target_deck mapping for the audio_dir path
    # where manifest _idx may not match sync-time new_words ordering
    # (prefetch has empty existing_map, so new_words differs).
    word_id_to_target_deck: dict[str, str] = {}
    for idx, w in enumerate(new_words):
        lvl = _get_target_level_for_word(w)
        if lvl is not None and lvl in level_to_band:
            band = level_to_band[lvl]
            target_name = _build_subdeck_name(deck_name, band)
            word_target_deck[idx] = target_name
        else:
            target_name = deck_name  # parent deck fallback
            word_target_deck[idx] = target_name
        word_id_to_target_deck[_make_word_id(w, namespace_id)] = target_name

    # 6. Print deck plan (informational, doesn't block)
    if bands:
        _print_deck_plan(data, deck_name, level_new_counts, bands)

    # Print level distribution
    if bands:
        level_dist: dict[str, int] = {}
        for deck in word_target_deck.values():
            short = deck.split("::")[-1] if "::" in deck else deck
            level_dist[short] = level_dist.get(short, 0) + 1
        print(f"  New word distribution: "
              f"{', '.join(f'{k}: {v}' for k, v in level_dist.items())}")

    print(f"\n  New words to add: {len(new_words)}")
    print(f"  Already in deck: {len(skipped_words)}")

    # 7. Create sub-decks
    if bands and not prefetch and not dry_run:
        for band in bands:
            sub_deck = _build_subdeck_name(deck_name, band)
            ac.ensure_deck_and_model(sub_deck, MODEL_NAME)

    # 8. Migrate misplaced cards
    migration_count = 0
    if bands and not prefetch and not dry_run:
        print(f"\nChecking for misplaced cards...")
        migration_count = _migrate_misplaced_cards(ac, deck_name, bands, existing_map)
        if migration_count:
            print(f"  Migrated {migration_count} card(s) to correct sub-decks")

    if not new_words:
        print("\nDeck is up to date — nothing to add.")
        return {
            "added": 0,
            "skipped": len(skipped_words),
            "audio_uploaded": 0,
            "words_added": [],
            "words_skipped": [w["word"] for w in skipped_words],
            "migrated": migration_count,
        }

    if dry_run:
        print(f"\n[DRY RUN] Would add:")
        for w in new_words:
            print(f"  + {w['word']}")
        return {
            "added": len(new_words),
            "skipped": len(skipped_words),
            "audio_uploaded": 0,
            "words_added": [w["word"] for w in new_words],
            "words_skipped": [w["word"] for w in skipped_words],
        }

    # 9. Generate audio and build notes for new words (parallel)
    if prefetch:
        print(f"\nPrefetching audio for {len(new_words)} new words (parallel)...")
    else:
        print(f"\nGenerating audio for {len(new_words)} new words (parallel)...")
    notes_to_add = []
    audio_uploads: list[tuple[str, bytes]] = []
    failed_words: list[str] = []
    word_ok = 0
    sent_ok = 0
    total = len(new_words)

    if audio_dir:
        # Load pre-generated audio from disk and build notes
        manifest_path = os.path.join(audio_dir, "manifest.json")
        if not os.path.isfile(manifest_path):
            print(f"Error: manifest not found at {manifest_path}", file=sys.stderr)
            sys.exit(1)
        with open(manifest_path, "r", encoding="utf-8") as mf:
            manifest = json.load(mf)
        # Restore notes (without deckName — fill in below).
        # Match by WordId (not _idx) because prefetch has empty
        # existing_map so its new_words/index differ from sync.
        # Only load notes for words in the current new_words set —
        # the manifest may contain stale entries from a previous
        # prefetch run that included already-known words.
        new_word_ids = {_make_word_id(w, namespace_id) for w in new_words}
        for i, note in enumerate(manifest["notes"]):
            wid = note.get("fields", {}).get("WordId", "")
            if wid and wid not in new_word_ids:
                continue  # stale entry from a different prefetch batch
            note["deckName"] = word_id_to_target_deck.get(
                wid,
                word_target_deck.get(note.get("_idx", i), deck_name),
            )
            notes_to_add.append(note)
        # Load audio files from disk — filter against new_word_ids.
        # Audio filenames: {safe_lemma}_{POS}_{book_id}_word.mp3 or _sent.mp3
        # WordId = {safe_filename(lemma)}_{POS}_{book_id}, same as filename prefix.
        for filename, filepath in manifest["audio_files"].items():
            if filename.endswith("_word.mp3"):
                wid = filename[:-len("_word.mp3")]
            elif filename.endswith("_sent.mp3"):
                wid = filename[:-len("_sent.mp3")]
            else:
                continue  # unknown pattern, skip
            if wid not in new_word_ids:
                if verbose:
                    print(f"  SKIP audio {filename} (stale — WordId not in new_words)")
                continue
            if os.path.isfile(filepath):
                with open(filepath, "rb") as af:
                    audio_uploads.append((filename, af.read()))
        word_ok = manifest["stats"]["word_ok"]
        sent_ok = manifest["stats"]["sent_ok"]
        print(f"  Loaded {len(notes_to_add)} notes + {len(audio_uploads)} audio files from cache")
    else:
        # Generate audio concurrently
        max_workers = min(16, len(new_words))
        if verbose:
            print(f"  ({max_workers} workers)")

        # Inject _idx before thread pool so each note preserves its
        # original position for correct band assignment.
        for idx, w in enumerate(new_words):
            w["_idx"] = idx

        with concurrent.futures.ThreadPoolExecutor(max_workers=max_workers) as executor:
            future_map = {
                executor.submit(_process_one_word, w, namespace_id, no_audio, word_timeout): (idx, w)
                for idx, w in enumerate(new_words)
            }

            completed = 0
            for future in concurrent.futures.as_completed(future_map):
                idx, w = future_map[future]
                word = w["word"]
                lemma = w.get("lemma", "").strip() or word
                completed += 1

                try:
                    note, word_audio, final_ipa = future.result(timeout=word_timeout)
                    note["deckName"] = word_target_deck.get(idx, deck_name)
                    notes_to_add.append(note)
                    audio_uploads.extend(word_audio)

                    has_word = any(fn.endswith("_word.mp3") for fn, _ in word_audio)
                    has_sent = any(fn.endswith("_sent.mp3") for fn, _ in word_audio)
                    if has_word:
                        word_ok += 1
                    if has_sent:
                        sent_ok += 1

                    tag = f" ({lemma})" if lemma != word.lower() else ""
                    label = f"{word}{tag}"
                    if verbose and not no_audio:
                        parts = []
                        parts.append("word✓" if has_word else "word✗")
                        parts.append("sent✓" if has_sent else "sent✗")
                        label = f"{label}  audio: {', '.join(parts)}"
                    print_progress(completed, total, label)
                except Exception as e:
                    failed_words.append(word)
                    print()
                    print(f"  ✗ {word}: {e}")
                    print_progress(completed, total, word)

        print()

        # Mechanical check: verify every note's WordId matches _make_word_id.
        # This catches drift between build_note_entry and the dedup check —
        # both must use the same resolved-lemma WordId format.
        for note in notes_to_add:
            wid = note["fields"]["WordId"]
            # Reconstruct expected WordId from the same input that
            # build_note_entry received.  We don't have the original
            # word_data here, so validate by checking the WordId uses
            # the safe_filename(Word) pattern (Word field = resolve_lemma result).
            card_word = note["fields"]["Word"]
            expected_prefix = safe_filename(card_word)
            if not wid.startswith(expected_prefix + "_"):
                raise AssertionError(
                    f"WordId mismatch: note WordId={wid!r} but card Word={card_word!r} "
                    f"(expected WordId to start with {expected_prefix!r}_)"
                )

        # If prefetch mode: save to temp dir with manifest, then exit
        if prefetch:
            prefetch_dir = tempfile.mkdtemp(prefix="vocab_audio_")
            audio_files: dict[str, str] = {}

            for filename, data in audio_uploads:
                filepath = os.path.join(prefetch_dir, filename)
                with open(filepath, "wb") as af:
                    af.write(data)
                audio_files[filename] = filepath

            # Strip deckName from notes (will be filled in at sync time)
            saved_notes = []
            for note in notes_to_add:
                saved = dict(note)
                saved.pop("deckName", None)
                saved_notes.append(saved)

            manifest = {
                "notes": saved_notes,
                "audio_files": audio_files,
                "stats": {
                    "total": total,
                    "word_ok": word_ok,
                    "sent_ok": sent_ok,
                    "failed": len(failed_words),
                },
                "failed_words": failed_words,
            }
            manifest_path = os.path.join(prefetch_dir, "manifest.json")
            with open(manifest_path, "w", encoding="utf-8") as mf:
                json.dump(manifest, mf, indent=2, ensure_ascii=False)

            print(f"\nAudio prefetch complete:")
            print(f"  Word audio:  {word_ok}/{total}")
            print(f"  Sentence:    {sent_ok}/{total}")
            if failed_words:
                raise RuntimeError(
                    f"Audio generation failed for {len(failed_words)} word(s) "
                    f"after {EDGE_TTS_MAX_RETRIES + 1} attempts each: "
                    f"{', '.join(failed_words)}"
                )
            print(f"\nAUDIO_DIR={prefetch_dir}")
            return {
                "added": 0,
                "skipped": len(skipped_words),
                "audio_uploaded": len(audio_uploads),
                "words_added": [],
                "words_skipped": [w["word"] for w in skipped_words],
                "failed_words": failed_words,
                "prefetch_dir": prefetch_dir,
                "prefetch_stats": manifest["stats"],
            }

    # 7. Upload media files (direct filesystem copy, fallback to AnkiConnect)
    audio_count = 0
    if audio_uploads and not dry_run:
        total_media = len(audio_uploads)
        media_dir = None if no_direct_media else _find_anki_media_dir()

        if media_dir:
            # Fast path: copy files directly into collection.media (~0.7s for 124 files)
            print(f"\nCopying {total_media} media files to Anki media dir...")
            if verbose:
                print(f"  {media_dir}")
            audio_count = _upload_media_direct(audio_uploads, media_dir, verbose=verbose)
            print()
            print(f"  Copied: {audio_count}/{total_media}")
        else:
            # Slow path: upload through AnkiConnect (per-file base64 + SQLite overhead)
            batch_size = 40
            print(f"\nUploading {total_media} media files via AnkiConnect (batched ×{batch_size})...")
            for batch_start in range(0, total_media, batch_size):
                batch = audio_uploads[batch_start:batch_start + batch_size]
                try:
                    results = ac.store_media_files_batch(batch, batch_size=batch_size)
                    for i, result in enumerate(results):
                        filename, audio_data = batch[i]
                        global_idx = batch_start + i + 1
                        if result is not None:
                            audio_count += 1
                            if verbose:
                                print_progress(global_idx, total_media, f"{filename} ({len(audio_data)} bytes)")
                            else:
                                print_progress(global_idx, total_media)
                        else:
                            print()
                            print(f"  ✗ {filename}: failed (duplicate or error)")
                            print_progress(global_idx, total_media)
                except AnkiConnectError as e:
                    for i, (filename, _data) in enumerate(batch):
                        global_idx = batch_start + i + 1
                        print()
                        print(f"  ✗ {filename}: {e}")
                        print_progress(global_idx, total_media)
            print()
            print(f"  Uploaded: {audio_count}/{total_media}")

    # 8. Add notes
    added_count = 0
    duped_count = 0
    if not dry_run and notes_to_add:
        print(f"\nAdding {len(notes_to_add)} new cards to Anki...")
        try:
            # Strip internal _idx before sending to AnkiConnect
            for note in notes_to_add:
                note.pop("_idx", None)
            result = ac.add_notes(notes_to_add)
            added_count = sum(1 for r in result if r is not None)
            duped_count = sum(1 for r in result if r is None)
            print(f"  Added: {added_count}")
            if duped_count:
                print(f"  Duplicates (not added): {duped_count}")
        except AnkiConnectError as e:
            errmsg = str(e)
            if "duplicate" in errmsg.lower():
                print(f"  All {len(notes_to_add)} already exist (duplicates).")
                duped_count = len(notes_to_add)
            else:
                print(f"Error adding notes: {e}")
                return {
                    "added": 0,
                    "skipped": len(skipped_words),
                    "audio_uploaded": audio_count,
                    "words_added": [],
                    "words_skipped": [w["word"] for w in skipped_words + new_words],
                    "failed_words": failed_words,
                    "error": errmsg,
                }

    # 9. Trigger AnkiWeb sync (fire-and-forget)
    ankiweb_synced = False
    if ac and not no_ankiweb_sync and not dry_run:
        try:
            ac.sync()
            ankiweb_synced = True
            print(f"\nAnkiWeb sync triggered.")
        except AnkiConnectError as e:
            print(f"\nAnkiWeb sync skipped: {e}")

    # Summary
    print(f"\n{'='*50}")
    print(f"Sync complete for \"{book_title}\"")
    print(f"  New cards added:  {added_count}")
    print(f"  Already in deck:  {len(skipped_words)}")
    print(f"  Media uploaded:   {audio_count}")
    if data.get("excluded"):
        print(f"  Excluded (COCA):  {len(data.get('excluded', []))} word(s)")
    if failed_words:
        raise RuntimeError(
            f"Audio generation failed for {len(failed_words)} word(s) "
            f"after {EDGE_TTS_MAX_RETRIES + 1} attempts each: "
            f"{', '.join(failed_words)}"
        )
    print(f"{'='*50}")

    return {
        "added": added_count,
        "skipped": len(skipped_words),
        "audio_uploaded": audio_count,
        "words_added": [w["word"] for w in new_words[:added_count]],
        "words_skipped": [w["word"] for w in skipped_words],
        "failed_words": failed_words,
        "ankiweb_synced": ankiweb_synced,
    }


