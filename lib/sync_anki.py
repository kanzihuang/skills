# ARPAbet → IPA conversion gold-standard tests
# ---------------------------------------------------------------------------


def _test_arpabet_to_ipa() -> None:
    """Verify ARPAbet→IPA mapping against known-correct gold standards.

    Never remove or weaken a test case — the mapping is the single source
    of truth for all card IPA generation.
    """
    failures = 0

    def check(word: str, expected: str) -> None:
        nonlocal failures
        got = _cmu_ipa(word)  # no Claude IPA → uses first entry
        if got != expected:
            failures += 1
            print(f"  FAIL: {word} → {got} (expected {expected})", file=sys.stderr)

    # ── Vowel length distinctions (tense vs lax) ──
    #   Monosyllabic: no stress mark (standard IPA convention)
    check("feed", "/fiːd/")          # IY → iː
    check("fed", "/fed/")            # EH → e
    check("fit", "/fɪt/")            # IH → ɪ
    check("food", "/fuːd/")          # UW → uː
    check("foot", "/fʊt/")           # UH → ʊ
    check("fast", "/fæst/")          # AE → æ
    check("father", "/ˈfɑːðər/")     # AA → ɑː, ER0 → ər
    check("law", "/lɔː/")            # AO → ɔː, unambiguous in cmudict
    check("cup", "/kʌp/")            # AH → ʌ
    check("bird", "/bɜːrd/")         # ER1 → ɜːr (monosyllabic, no stress)

    # ── Diphthongs ──
    check("day", "/deɪ/")            # EY → eɪ
    check("go", "/ɡoʊ/")             # OW → oʊ
    check("my", "/maɪ/")             # AY → aɪ
    check("now", "/naʊ/")            # AW → aʊ
    check("boy", "/bɔɪ/")            # OY → ɔɪ

    # ── Consonants ──
    check("thin", "/θɪn/")           # TH → θ
    check("this", "/ðɪs/")           # DH → ð
    check("ship", "/ʃɪp/")           # SH → ʃ
    check("chip", "/tʃɪp/")          # CH → tʃ
    check("judge", "/dʒʌdʒ/")        # JH → dʒ
    check("sing", "/sɪŋ/")           # NG → ŋ
    check("measure", "/ˈmeʒər/")     # ZH → ʒ, ER0 → ər
    check("yes", "/jes/")            # Y → j

    # ── Multi-syllable stress ──
    check("hunting", "/ˈhʌntɪŋ/")
    check("accomplished", "/əˈkɑːmplɪʃt/")
    check("distinguished", "/dɪˈstɪŋɡwɪʃt/")
    check("beautiful", "/ˈbjuːtəfəl/")
    check("blundering", "/ˈblʌndərɪŋ/")
    check("comfortable", "/ˈkʌmfərtəbəl/")

    if failures:
        print(f"\n  {failures} ARPAbet→IPA test(s) FAILED", file=sys.stderr)
        sys.exit(1)
    else:
        print(f"  ARPAbet→IPA: all {29} tests passed ✓")


if __name__ == "__main__":
    main()
