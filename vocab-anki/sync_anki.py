def _process_one_word(
    w: dict,
    book_id: str,
    no_audio: bool,
) -> tuple[dict, list[tuple[str, bytes]], str]:
    """Generate audio and build a note for a single word.

    Runs in a thread so it can be subject to a per-word timeout.

    Returns (note, audio_uploads, ipa).
    """
    word = w["word"]
    lemma = lemmatize_word(word)
    safe = safe_filename(lemma)
    ipa = w.get("ipa", "")
    audio_uploads: list[tuple[str, bytes]] = []

    if not no_audio:
        # Word audio: JSON IPA → SSML (skip API) → Free Dictionary API → Edge TTS fallback
        if ipa:
            tts = edge_tts_bytes(lemma, ipa=ipa)
            if tts:
                audio_uploads.append((f"{safe}_word.mp3", tts))
        else:
            fetched_ipa, _audio_url, api_audio = fetch_word_data(lemma)
            if api_audio:
                audio_uploads.append((f"{safe}_word.mp3", api_audio))
                if fetched_ipa:
                    ipa = fetched_ipa
            else:
                fallback_ipa = fetched_ipa or None
                tts = edge_tts_bytes(lemma, ipa=fallback_ipa)
                if tts:
                    audio_uploads.append((f"{safe}_word.mp3", tts))

        # Sentence audio: Edge TTS on cleaned sentence
        clean = re.sub(r"<[^>]+>", "", w["sentence"])
        sent_tts = edge_tts_bytes(clean)
        if sent_tts:
            audio_uploads.append((f"{safe}_sent.mp3", sent_tts))

    note = build_note_entry(w, ipa, book_id, lemma=lemma)
    return note, audio_uploads, ipa


def build_note_entry(
    word_data: dict,
    ipa: str | None,
    book_id: str,
    lemma: str | None = None,
) -> dict:
    """Build a single AnkiConnect note dict from word data.

    Audio files are referenced as [sound:filename.mp3] — the actual
    media files must be uploaded separately via store_media_file().

    If lemma is provided, it's used for the card Word field, audio filenames,
    and WordId. The original form is only preserved in the sentence.
    """
    display = (lemma or word_data["word"]).strip().lower()
    safe = safe_filename(display)
    word_audio_ref = f"[sound:{safe}_word.mp3]"
    sent_audio_ref = f"[sound:{safe}_sent.mp3]"
    word_id = f"{display}_{book_id}"

    return {
        "deckName": "",  # filled in later by caller
        "modelName": MODEL_NAME,
        "fields": {
            "WordId": word_id,
            "Word": display,
            "Sentence": word_data["sentence"],
            "IPA": ipa or word_data.get("ipa", ""),
            "DefinitionCN": word_data["definition_cn"],
            "TranslationCN": word_data["translation_cn"],
            "WordAudio": word_audio_ref,
            "SentenceAudio": sent_audio_ref,
        },
        "tags": ["weread"],
    }


def sync(
    data: dict,
    deck_name: str,
    dry_run: bool = False,
    no_audio: bool = False,
    verbose: bool = False,
    word_timeout: int = WORD_TIMEOUT,
    prefetch: bool = False,
    audio_dir: str | None = None,
) -> dict:
    """Run the full sync workflow. Returns a summary dict."""
    words = data["words"]
    book_title = data["book_title"]

    book_id = data.get("book_id", "")

    # 1. Connect to Anki (skip for prefetch — audio-only mode)
    ac = None
    if not prefetch:
        print(f'Connecting to AnkiConnect...')
        ac = AnkiConnect()

        # 2. Ensure deck and model exist
        ac.ensure_deck_and_model(deck_name, MODEL_NAME)
        print(f'  Deck: "{deck_name}"')
        print(f"  Model: {MODEL_NAME}")

        # 3. Get existing WordId map from target deck
        print(f"\nQuerying existing cards in deck...")
        existing = ac.get_word_id_map(deck_name)
        print(f"  Found {len(existing)} existing cards")
    else:
        existing = {}

    # 4. Identify new words (check lemma-based WordId, fallback to original)
    new_words = []
    skipped_words = []
    for w in words:
        lemma = lemmatize_word(w["word"])
        original_id = f"{w['word'].strip().lower()}_{book_id}"
        lemma_id = f"{lemma}_{book_id}"
        if lemma_id in existing or original_id in existing:
            skipped_words.append(w)
            if verbose:
                print(f"  SKIP {w['word']} (already in deck)")
        else:
            new_words.append(w)

    print(f"\n  New words to add: {len(new_words)}")
    print(f"  Already in deck: {len(skipped_words)}")

    if not new_words:
        print("\nDeck is up to date — nothing to add.")
        return {
            "added": 0,
            "skipped": len(skipped_words),
            "audio_uploaded": 0,
            "words_added": [],
            "words_skipped": [w["word"] for w in skipped_words],
        }

    if dry_run:
        print(f"\n[DRY RUN] Would add:")
        for w in new_words:
            print(f"  + {w['word']}")
        return {
            "added": len(new_words),
            "skipped": len(skipped_words),
            "audio_uploaded": 0,
            "words_added": [w["word"] for w in new_words],
            "words_skipped": [w["word"] for w in skipped_words],
        }

    # 5. Generate audio and build notes for new words (parallel)
    if prefetch:
        print(f"\nPrefetching audio for {len(new_words)} new words (parallel)...")
    else:
        print(f"\nGenerating audio for {len(new_words)} new words (parallel)...")
    notes_to_add = []
    audio_uploads: list[tuple[str, bytes]] = []
    failed_words: list[str] = []
    word_ok = 0
    sent_ok = 0
    total = len(new_words)

    if audio_dir:
        # Load pre-generated audio from disk and build notes
        manifest_path = os.path.join(audio_dir, "manifest.json")
        if not os.path.isfile(manifest_path):
            print(f"Error: manifest not found at {manifest_path}", file=sys.stderr)
            sys.exit(1)
        with open(manifest_path, "r", encoding="utf-8") as mf:
            manifest = json.load(mf)
        # Restore notes (without deckName — fill in below)
        for note in manifest["notes"]:
            note["deckName"] = deck_name
            notes_to_add.append(note)
        # Load audio files from disk
        for filename, filepath in manifest["audio_files"].items():
            if os.path.isfile(filepath):
                with open(filepath, "rb") as af:
                    audio_uploads.append((filename, af.read()))
        word_ok = manifest["stats"]["word_ok"]
        sent_ok = manifest["stats"]["sent_ok"]
        print(f"  Loaded {len(notes_to_add)} notes + {len(audio_uploads)} audio files from cache")
    else:
        # Generate audio concurrently
        max_workers = min(8, len(new_words))
        if verbose:
            print(f"  ({max_workers} workers)")

        with concurrent.futures.ThreadPoolExecutor(max_workers=max_workers) as executor:
            future_map = {
                executor.submit(_process_one_word, w, book_id, no_audio): (i, w)
                for i, w in enumerate(new_words, 1)
            }

            completed = 0
            for future in concurrent.futures.as_completed(future_map):
                i, w = future_map[future]
                word = w["word"]
                lemma = lemmatize_word(word)
                completed += 1

                try:
                    note, word_audio, final_ipa = future.result()
                    note["deckName"] = deck_name
                    notes_to_add.append(note)
                    audio_uploads.extend(word_audio)

                    has_word = any(fn.endswith("_word.mp3") for fn, _ in word_audio)
                    has_sent = any(fn.endswith("_sent.mp3") for fn, _ in word_audio)
                    if has_word:
                        word_ok += 1
                    if has_sent:
                        sent_ok += 1

                    tag = f" ({lemma})" if lemma != word.lower() else ""
                    label = f"{word}{tag}"
                    if verbose and not no_audio:
                        parts = []
                        parts.append("word✓" if has_word else "word✗")
                        parts.append("sent✓" if has_sent else "sent✗")
                        label = f"{label}  audio: {', '.join(parts)}"
                    print_progress_bar(completed, total, label)
                except Exception as e:
                    failed_words.append(word)
                    print()
                    print(f"  ✗ {word}: {e}")
                    print_progress_bar(completed, total, word)

        print()

        # If prefetch mode: save to temp dir with manifest, then exit
        if prefetch:
            prefetch_dir = tempfile.mkdtemp(prefix="vocab_audio_")
            audio_files: dict[str, str] = {}

            for filename, data in audio_uploads:
                filepath = os.path.join(prefetch_dir, filename)
                with open(filepath, "wb") as af:
                    af.write(data)
                audio_files[filename] = filepath

            # Strip deckName from notes (will be filled in at sync time)
            saved_notes = []
            for note in notes_to_add:
                saved = dict(note)
                saved.pop("deckName", None)
                saved_notes.append(saved)

            manifest = {
                "notes": saved_notes,
                "audio_files": audio_files,
                "stats": {
                    "total": total,
                    "word_ok": word_ok,
                    "sent_ok": sent_ok,
                    "failed": len(failed_words),
                },
                "failed_words": failed_words,
            }
            manifest_path = os.path.join(prefetch_dir, "manifest.json")
            with open(manifest_path, "w", encoding="utf-8") as mf:
                json.dump(manifest, mf, indent=2, ensure_ascii=False)

            print(f"\nAudio prefetch complete:")
            print(f"  Word audio:  {word_ok}/{total}")
            print(f"  Sentence:    {sent_ok}/{total}")
            if failed_words:
                print(f"  Failed:      {len(failed_words)} ({', '.join(failed_words)})")
            print(f"\nAUDIO_DIR={prefetch_dir}")
            return {
                "added": 0,
                "skipped": len(skipped_words),
                "audio_uploaded": len(audio_uploads),
                "words_added": [],
                "words_skipped": [w["word"] for w in skipped_words],
                "failed_words": failed_words,
                "prefetch_dir": prefetch_dir,
                "prefetch_stats": manifest["stats"],
            }

    # 6. Upload media files
    audio_count = 0
    if audio_uploads and not dry_run:
        total_media = len(audio_uploads)
        print(f"\nUploading {total_media} media files...")
        for i_media, (filename, data) in enumerate(audio_uploads, 1):
            try:
                ac.store_media_file(filename, data)
                audio_count += 1
                if verbose:
                    print_progress_bar(i_media, total_media, f"{filename} ({len(data)} bytes)")
                else:
                    print_progress_bar(i_media, total_media)
            except AnkiConnectError as e:
                # New line before error, then resume bar
                print()
                print(f"  ✗ {filename}: {e}")
                print_progress_bar(i_media, total_media)
        # Finalize media progress bar
        print()
        print(f"  Uploaded: {audio_count}/{total_media}")

    # 7. Add notes
    added_count = 0
    duped_count = 0
    if not dry_run and notes_to_add:
        print(f"\nAdding {len(notes_to_add)} new cards to Anki...")
        try:
            result = ac.add_notes(notes_to_add)
            added_count = sum(1 for r in result if r is not None)
            duped_count = sum(1 for r in result if r is None)
            print(f"  Added: {added_count}")
            if duped_count:
                print(f"  Duplicates (not added): {duped_count}")
        except AnkiConnectError as e:
            errmsg = str(e)
            if "duplicate" in errmsg.lower():
                print(f"  All {len(notes_to_add)} already exist (duplicates).")
                duped_count = len(notes_to_add)
            else:
                print(f"Error adding notes: {e}")
                return {
                    "added": 0,
                    "skipped": len(skipped_words),
                    "audio_uploaded": audio_count,
                    "words_added": [],
                    "words_skipped": [w["word"] for w in skipped_words + new_words],
                    "failed_words": failed_words,
                    "error": errmsg,
                }

    # Summary
    print(f"\n{'='*50}")
    print(f"Sync complete for \"{book_title}\"")
    print(f"  New cards added:  {added_count}")
    print(f"  Already in deck:  {len(skipped_words)}")
    print(f"  Media uploaded:   {audio_count}")
    if failed_words:
        print(f"  Failed:           {len(failed_words)} word(s)")
        print(f"     ({', '.join(failed_words)})")
    print(f"{'='*50}")

    return {
        "added": added_count,
        "skipped": len(skipped_words),
        "audio_uploaded": audio_count,
        "words_added": [w["word"] for w in new_words[:added_count]],
        "words_skipped": [w["word"] for w in skipped_words],
        "failed_words": failed_words,
    }


