def main() -> None:
    args = parse_args()

    # Read input
    with open(args.input_file, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Basic validation
    if not data.get("book_title"):
        print("Error: missing 'book_title' in input", file=sys.stderr)
        sys.exit(1)
    if not data.get("book_id"):
        print("Error: missing 'book_id' in input", file=sys.stderr)
        sys.exit(1)
    if not data.get("words"):
        print("Error: 'words' array is empty", file=sys.stderr)
        sys.exit(1)

    # Deduplicate
    seen = set()
    deduped = []
    for w in data["words"]:
        key = w["word"].strip().lower()
        if key not in seen:
            seen.add(key)
            deduped.append(w)
    if len(deduped) < len(data["words"]):
        print(f"Note: deduplicated {len(data['words']) - len(deduped)} word(s)\n")
    data["words"] = deduped

    # Pre-fill IPA from cmudict before validation.
    # Claude may leave ipa empty — cmudict fills it automatically for known words.
    # Only words truly not in cmudict will still have empty ipa and fail validation
    # (requiring Claude to provide a manual IPA).
    for w in data["words"]:
        if not w.get("ipa", "").strip():
            json_lemma = w.get("lemma", "").strip()
            lookup_word = json_lemma if json_lemma else w["word"]
            cmu_ipa = _cmu_ipa(lookup_word)
            if cmu_ipa:
                w["ipa"] = cmu_ipa

    # Validate quality rules before sync
    errors = _validate_word_entries(data["words"])
    if errors:
        print(f"Quality check FAILED ({len(errors)} error(s)):", file=sys.stderr)
        for e in errors:
            print(f"  {e}", file=sys.stderr)
        sys.exit(1)

    # Deck name resolution priority:
    #   1. JSON deck_name field (set by Claude from Step 0b {deck: bookId} mapping)
    #   2. --deck CLI flag (explicit override)
    #   3. Auto-derive from book_title + book_author (new deck, fallback)
    json_deck = data.get("deck_name", "").strip()
    if json_deck:
        deck_name = json_deck
    elif args.deck:
        deck_name = args.deck
    else:
        author = data.get("book_author", "")
        title = data["book_title"]
        deck_name = f"{title} ({author})" if author else f"{title} Vocabulary"

    try:
        result = sync(
            data,
            deck_name,
            dry_run=args.dry_run,
            no_audio=args.no_audio,
            verbose=args.verbose,
            word_timeout=args.word_timeout,
            prefetch=args.prefetch,
            audio_dir=args.audio_dir,
            no_direct_media=args.no_direct_media,
            no_ankiweb_sync=args.no_ankiweb_sync,
            max_bands=args.max_bands,
            min_band_size=args.min_band_size,
        )
        if result.get("error"):
            sys.exit(1)
    except AnkiConnectError as e:
        print(f"\nError: {e}", file=sys.stderr)
        print(
            "\nMake sure:\n"
            "  1. Anki is running\n"
            '  2. AnkiConnect plugin is installed (add-on code: 2055492159)\n'
            "  3. The Vocabulary Card (WeRead) model exists in Anki\n"
            "     (import an .apkg file from this skill first)",
            file=sys.stderr,
        )
        sys.exit(1)


# ---------------------------------------------------------------------------
# ARPAbet → IPA conversion gold-standard tests
# ---------------------------------------------------------------------------


def _test_arpabet_to_ipa() -> None:
    """Verify ARPAbet→IPA mapping against known-correct gold standards.

    Never remove or weaken a test case — the mapping is the single source
    of truth for all card IPA generation.
    """
    failures = 0

    def check(word: str, expected: str) -> None:
        nonlocal failures
        got = _cmu_ipa(word)  # no Claude IPA → uses first entry
        if got != expected:
            failures += 1
            print(f"  FAIL: {word} → {got} (expected {expected})", file=sys.stderr)

    # ── Vowel length distinctions (tense vs lax) ──
    #   Monosyllabic: no stress mark (standard IPA convention)
    check("feed", "/fiːd/")          # IY → iː
    check("fed", "/fed/")            # EH → e
    check("fit", "/fɪt/")            # IH → ɪ
    check("food", "/fuːd/")          # UW → uː
    check("foot", "/fʊt/")           # UH → ʊ
    check("fast", "/fæst/")          # AE → æ
    check("father", "/fˈɑːðɜːr/")   # AA → ɑː, 2-syll → stress
    check("law", "/lɔː/")            # AO → ɔː, unambiguous in cmudict
    check("cup", "/kʌp/")            # AH → ʌ
    check("bird", "/bɜːrd/")         # ER → ɜːr

    # ── Diphthongs ──
    check("day", "/deɪ/")            # EY → eɪ
    check("go", "/ɡoʊ/")             # OW → oʊ
    check("my", "/maɪ/")             # AY → aɪ
    check("now", "/naʊ/")            # AW → aʊ
    check("boy", "/bɔɪ/")            # OY → ɔɪ

    # ── Consonants ──
    check("thin", "/θɪn/")           # TH → θ
    check("this", "/ðɪs/")           # DH → ð
    check("ship", "/ʃɪp/")           # SH → ʃ
    check("chip", "/tʃɪp/")          # CH → tʃ
    check("judge", "/dʒʌdʒ/")        # JH → dʒ
    check("sing", "/sɪŋ/")           # NG → ŋ
    check("measure", "/mˈeʒɜːr/")   # ZH → ʒ, 2-syll → stress
    check("yes", "/jes/")            # Y → j

    # ── Multi-syllable stress ──
    check("hunting", "/hˈʌntɪŋ/")
    check("accomplished", "/ʌkˈɑːmplɪʃt/")
    check("distinguished", "/dɪstˈɪŋɡwɪʃt/")
    check("beautiful", "/bjˈuːtʌfʌl/")
    check("blundering", "/blˈʌndɜːrɪŋ/")
    check("comfortable", "/kˈʌmfɜːrtʌbʌl/")

    if failures:
        print(f"\n  {failures} ARPAbet→IPA test(s) FAILED", file=sys.stderr)
        sys.exit(1)
    else:
        print(f"  ARPAbet→IPA: all {23+5} tests passed ✓")


if __name__ == "__main__":
    main()
