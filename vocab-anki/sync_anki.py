MODEL_NAME = "Vocabulary Card (WeRead)"
WORD_TIMEOUT = 30  # default seconds per word (audio generation)
# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Sync WeRead vocabulary to Anki via AnkiConnect"
    )
    parser.add_argument("input_file", help="JSON input file with word entries")
    parser.add_argument(
        "--deck", "-d", required=False, help="Target Anki deck name (auto-derived from JSON if omitted)"
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Show what would be added without actually adding",
    )
    parser.add_argument(
        "--no-audio",
        action="store_true",
        help="Skip audio generation and upload",
    )
    parser.add_argument(
        "--word-timeout",
        type=int,
        default=WORD_TIMEOUT,
        help=f"Deprecated. API calls have built-in timeouts. (default: {WORD_TIMEOUT})",
    )
    parser.add_argument(
        "--prefetch",
        action="store_true",
        help="Generate audio concurrently and save to temp dir with manifest (no Anki interaction)",
    )
    parser.add_argument(
        "--audio-dir",
        required=False,
        help="Use pre-generated audio from directory (skips audio generation)",
    )
    parser.add_argument(
        "-v", "--verbose", action="store_true", help="Show detailed progress"
    )
    return parser.parse_args()


