# Words lemminflect can't distinguish as derivational adjectives.
_DERIVATIONAL_ADJ_BLOCKLIST: set[str] = set()
_SPACY_NLP = None  # cached spaCy model


def _get_spacy():
    """Load spaCy model (cached)."""
    global _SPACY_NLP
    if _SPACY_NLP is None:
        try:
            import spacy
            _SPACY_NLP = spacy.load("en_core_web_sm")
        except Exception:
            pass
    return _SPACY_NLP


def _spacy_lemma(word: str, sentence: str) -> str | None:
    """Use spaCy to lemmatize a word in sentence context.

    spaCy's POS tagger uses context to decide whether an -ed/-ing word
    is an adjective (keep surface form) or verb (reduce to stem).
    Returns the corrected lemma, or None if spaCy is unavailable.
    """
    nlp = _get_spacy()
    if nlp is None:
        return None
    w = word.lower()
    try:
        doc = nlp(sentence)
        for token in doc:
            if token.text.lower() == w:
                return token.lemma_.lower()
    except Exception:
        pass
    return None


def _load_derivational_adjectives() -> set[str]:
    """Load known derivational adjectives from shared data file."""
    global _DERIVATIONAL_ADJ_BLOCKLIST
    if _DERIVATIONAL_ADJ_BLOCKLIST:
        return _DERIVATIONAL_ADJ_BLOCKLIST
    try:
        from pathlib import Path
        path = (
            Path(__file__).resolve().parent.parent
            / "lib" / "data" / "derivational_adjectives.txt"
        )
        if path.exists():
            with open(path) as f:
                _DERIVATIONAL_ADJ_BLOCKLIST = {line.strip().lower() for line in f if line.strip()}
    except Exception:
        pass
    return _DERIVATIONAL_ADJ_BLOCKLIST


def _is_derivational_adj(word: str) -> bool:
    """Check if a word is likely a derivational adjective.

    Uses a comprehensive adjective list (loaded from shared data) and
    lemminflect's ADJ lemmatizer.  For -ing forms lemminflect is
    reliable; for -ed forms we rely on the adjective list because
    lemminflect can't distinguish past participles from true
    adjectives (it flags both *attached* and *wicked* the same way).
    """
    w = word.lower()
    blocklist = _load_derivational_adjectives()
    if w in blocklist:
        return True
    if not (w.endswith("ing") or w.endswith("ed")):
        return False
    try:
        from lemminflect import getLemma

        adj = getLemma(word, "ADJ")
        verb = getLemma(word, "VERB")
        adj_unchanged = adj and all(a == word for a in adj)
        verb_changes = verb and any(v != word for v in verb)
        # For -ed: only trust lemminflect if the word is ALSO
        # in the adjective list (prevents false positives like
        # attached→attach being blocked).
        if w.endswith("ed"):
            return False
        # For -ing: lemminflect is reliable
        return adj_unchanged and verb_changes
    except ImportError:
        return False
    try:
        from lemminflect import getLemma

        adj = getLemma(word, "ADJ")
        verb = getLemma(word, "VERB")
        adj_unchanged = adj and all(a == word for a in adj)
        verb_changes = verb and any(v != word for v in verb)
        return adj_unchanged and verb_changes
    except ImportError:
        return False


def resolve_lemma(word: str, json_lemma: str) -> str:
    """Resolve the canonical lemma for a word.

    Strategy (in priority order):

    1. Explicit override: *json_lemma* differs from the surface form
       → Claude set it deliberately (e.g. derivational adj).  Trust it.

    2. Auto-correct: *json_lemma* is empty or equals the surface form
       AND the word is not a derivational adjective → run
       lemmatize_word().

    3. IRREG fallback for cases lemmatize_word() misses.

    4. Otherwise keep the provided lemma (or surface form).

    Claude should only set *lemma* for derivational adjectives /
    deliberate overrides.  Regular inflectional forms should have an
    empty *lemma* — lemmatize_word() handles those automatically.
    """
    w = word.lower()
    jl = json_lemma.strip().lower() if json_lemma else ""

    # 1. Explicit override: lemma differs from surface form → trust Claude
    if jl and jl != w:
        return jl

    # 2. Regular -est/-er/-ier/-iest patterns (before lemmatize_word,
    #    which may over-reduce: lemminflect maps *happier* → *hap*).
    #    Only apply when the word is NOT already in COCA, preventing
    #    false reductions like beer→bee, anger→ange, sacred→sacre.
    _coca: set[str] = set()
    try:
        from lib.coca import load_coca
        _coca = load_coca()
    except ImportError:
        pass
    # -est/-iest suffixes are reliable (superlatives, near-zero false
    # positives).  -er/-ier can match nouns (beer, anger, fiber) so we
    # only apply those when the word is NOT already in COCA.
    if w not in _coca or w.endswith("est") or w.endswith("iest"):
        for sfx, slen in [("iest", 4), ("est", 3), ("ier", 3), ("er", 2)]:
            if w.endswith(sfx) and len(w) > slen + 1:
                stem = w[:-slen]
                if sfx.startswith("i"):
                    cand = stem + "y"          # happiest→happy, happier→happy
                else:
                    cand = stem                 # smallest→small, closer→close?
                    # Doubled consonant: biggest→big (only when the stem ends
                    # in CVC where C is the doubled consonant & not a true
                    # double-letter ending like -ll, -ss, -ff in the base).
                    if len(stem) >= 3 and stem[-1] == stem[-2] and stem[-1] not in "aeiouyls":
                        cand2 = stem[:-1]
                        cand = cand2
                    # Dropped-e: closest→close (only if stem doesn't already
                    # look like a valid word, e.g. *small* from *smallest*).
                    if cand == stem and not (
                        len(stem) >= 2
                        and stem[-1] == stem[-2]
                        and stem[-1] not in "aeiouy"
                    ):
                        cand_e = stem + "e"
                        if cand_e in _coca and cand_e != w:
                            cand = cand_e
                if cand != w and len(cand) < len(w):
                    return cand
                break

    # 3. Auto-correct inflectional forms (unless derivational adjective).
    #    Apply COCA gate: only reduce if the word is NOT already in COCA,
    #    OR the reduced form is also a valid COCA entry.  This prevents
    #    false reductions like sacred→sacre, tremendous→tremendou where
    #    lemminflect hallucinates a non-existent verb base.
    if not _is_derivational_adj(word):
        reduced = lemmatize_word(word)
        if reduced != w:
            if w not in _coca or reduced in _coca:
                return reduced

    # 4. lemmatize_word couldn't reduce — try lib's IRREG dict
    try:
        from lib.lemmatize import IRREG  # type: ignore[import-not-found]

        if w in IRREG:
            return IRREG[w]
    except ImportError:
        pass

    # 5. Keep as-is
    return jl or w


def _process_one_word(
    w: dict,
    book_id: str,
    no_audio: bool,
) -> tuple[dict, list[tuple[str, bytes]], str]:
    """Generate audio and build a note for a single word.

    Runs in a thread so it can be subject to a per-word timeout.

    Returns (note, audio_uploads, ipa).
    """
    word = w["word"]
    json_lemma = w.get("lemma", "").strip()
    lemma = resolve_lemma(word, json_lemma)

    # Safety net: for -ed/-ing words, verify with spaCy's sentence-context
    # lemmatizer.  If spaCy says the word is an adjective (surface form),
    # override the mechanical reduction.  This prevents crossing like
    # distinguished→distinguish, wicked→wick.
    wl = word.lower()
    if lemma != wl and wl.endswith(("ed", "ing")):
        sent = w.get("sentence", "")
        if sent:
            spacy_result = _spacy_lemma(word, sent)
            if spacy_result and spacy_result == wl:
                lemma = wl

    safe = safe_filename(lemma)
    ipa = w.get("ipa", "")
    audio_uploads: list[tuple[str, bytes]] = []

    if not no_audio:
        # Word audio: Claude IPA → SSML synthesis; no IPA → skip word audio
        if ipa:
            tts = edge_tts_bytes(lemma, ipa=ipa)
            if tts:
                audio_uploads.append((f"{safe}_word.mp3", tts))

        # Sentence audio: Edge TTS on cleaned sentence
        clean = re.sub(r"<[^>]+>", "", w["sentence"])
        sent_tts = edge_tts_bytes(clean)
        if sent_tts:
            audio_uploads.append((f"{safe}_sent.mp3", sent_tts))

    note = build_note_entry(w, ipa, book_id, lemma=lemma)
    return note, audio_uploads, ipa


def build_note_entry(
    word_data: dict,
    ipa: str | None,
    book_id: str,
    lemma: str | None = None,
) -> dict:
    """Build a single AnkiConnect note dict from word data.

    Audio files are referenced as [sound:filename.mp3] — the actual
    media files must be uploaded separately via store_media_file().

    If lemma is provided, it's used for the card Word field, audio filenames,
    and WordId. The original form is only preserved in the sentence.
    """
    display = (lemma or word_data["word"]).strip().lower()
    safe = safe_filename(display)
    word_audio_ref = f"[sound:{safe}_word.mp3]"
    sent_audio_ref = f"[sound:{safe}_sent.mp3]"
    word_id = f"{display}_{book_id}"

    return {
        "deckName": "",  # filled in later by caller
        "modelName": MODEL_NAME,
        "fields": {
            "WordId": word_id,
            "Word": display,
            "Sentence": word_data["sentence"],
            "IPA": ipa or word_data.get("ipa", ""),
            "DefinitionCN": word_data["definition_cn"],
            "TranslationCN": word_data["translation_cn"],
            "WordAudio": word_audio_ref,
            "SentenceAudio": sent_audio_ref,
        },
        "tags": ["weread"],
    }


def _validate_word_entries(words: list[dict]) -> list[str]:
    """Validate word entries before sync. Returns list of error messages (empty = pass).

    Checks sentence length (≤150 chars), <b> tag matches word field,
    word actually appears in sentence text (after stripping tags),
    and required fields (ipa, definition_cn, translation_cn) are non-empty.

    Soft warnings (IPA format, definition sanity) are printed to stderr
    directly and do NOT block sync.
    """
    errors = []
    for w in words:
        word = w.get("word", "")
        sentence = w.get("sentence", "")

        # 1. <b> content must match word field (hard error)
        b_match = re.search(r"<b>(.*?)</b>", sentence)
        b_text = b_match.group(1) if b_match else ""
        if b_text != word:
            errors.append(f"[{word}] <b> mismatch: <b>{b_text}</b> ≠ word '{word}'")

        # 1b. <b> must wrap the COMPLETE surface word — detect tag-split
        #     errors like <b>dip</b>ped, <b>nail</b>s, <b>bind</b>ing
        if b_match and b_text:
            after = sentence[b_match.end():]
            if after and after[0].isalpha() and after[0].islower():
                remaining = ""
                for ch in after:
                    if ch.isalpha():
                        remaining += ch
                    else:
                        break
                full_word = b_text + remaining
                errors.append(
                    f"[{word}] <b> tag splits surface word: "
                    f"<b>{b_text}</b>{remaining} → should wrap complete "
                    f"surface form '<b>{full_word}</b>'"
                )

        # 2. Sentence must contain the word (case-insensitive, after stripping tags) — hard error
        clean_sentence = re.sub(r"<[^>]+>", "", sentence)
        if word.lower() not in clean_sentence.lower():
            errors.append(
                f"[{word}] word not found in sentence: "
                f"'{word}' not in '{clean_sentence[:80]}{'...' if len(clean_sentence) > 80 else ''}'"
            )

        # 3. Sentence length ≤ 150 chars (hard error)
        if len(sentence) > 150:
            errors.append(f"[{word}] sentence too long: {len(sentence)} chars (max 150)")

        # 4. Required fields non-empty (hard error)
        for field in ["ipa", "definition_cn", "translation_cn"]:
            if not w.get(field):
                errors.append(f"[{word}] missing '{field}'")

        # 4.5 lemma sanity: if explicitly set AND differs from both the
        #     surface word AND lemmatize_word(word) → almost certainly a
        #     mistake (e.g. beautiful→beautifully).  The correct pattern
        #     is to leave *lemma* empty for regular inflection and only
        #     set it for derivational-adjective overrides (lemma == word).
        json_lemma = w.get("lemma", "").strip()
        if json_lemma:
            resolved = resolve_lemma(word, "")
            machine = lemmatize_word(word)
            if json_lemma.lower() != word.lower() and json_lemma.lower() != machine.lower():
                errors.append(
                    f"[{word}] suspicious lemma '{json_lemma}': differs from both "
                    f"surface form '{word}' and lemmatize_word() result '{machine}'. "
                    f"Leave lemma empty for regular inflection (auto-correct → '{resolved}')"
                )

        # --- Soft warnings (stderr only, do not block sync) ---

        # 5. IPA format sanity check
        ipa = w.get("ipa", "")
        if ipa:
            ipa_clean = ipa.strip()
            if "/" not in ipa_clean:
                print(f"  [WARN] [{word}] IPA missing '/' delimiters: {ipa}", file=sys.stderr)
            else:
                # Strip delimiters and stress marks, count phoneme chars
                phonemes = re.sub(r"[/ˈˌ.]", "", ipa_clean)
                if len(phonemes) < 2:
                    print(f"  [WARN] [{word}] IPA too short: {ipa}", file=sys.stderr)
            if re.search(r"[一-鿿]", ipa_clean):
                print(f"  [WARN] [{word}] IPA contains Chinese characters: {ipa}", file=sys.stderr)

        # 6. Definition sanity check
        definition = w.get("definition_cn", "")
        if definition:
            cjk_count = len(re.findall(r"[一-鿿]", definition))
            if cjk_count < 2:
                print(f"  [WARN] [{word}] definition_cn has <2 Chinese chars: '{definition}'", file=sys.stderr)
            if definition.strip().lower() == word.lower():
                print(f"  [WARN] [{word}] definition_cn equals word: '{definition}'", file=sys.stderr)

        # 7. Sentence fragment detection (soft warnings, do not block sync)
        clean = re.sub(r"<[^>]+>", "", sentence).strip()
        if clean:
            # 7a. Starts with lowercase letter — suggests mid-sentence extraction
            first_char = clean[0]
            if first_char.isalpha() and first_char.islower():
                print(
                    f"  [WARN] [{word}] sentence starts with lowercase "
                    f"'{first_char}' — may be a truncated fragment",
                    file=sys.stderr,
                )

            # 7b. No finite verb detected — suggests noun phrase fragment
            has_finite_verb = bool(
                re.search(r'\b(?:is|are|was|were|am|has|have|had|do|does|did|'
                          r'will|would|can|could|shall|should|may|might|must)\b',
                          clean, re.IGNORECASE)
            )
            if not has_finite_verb:
                # Check for past tense / 3rd-person verbs: word ending in -ed, -s
                # (excluding common non-verb -s: this, his, us, thus, etc.)
                verb_ending = bool(
                    re.search(r'\b\w+(?:ed|s)\b', clean)
                )
                if not verb_ending:
                    print(
                        f"  [WARN] [{word}] sentence may lack a finite verb "
                        f"— possible noun phrase fragment: '{clean[:80]}{"..." if len(clean) > 80 else ""}'",
                        file=sys.stderr,
                    )

    return errors


def _find_anki_media_dir() -> str | None:
    """Locate Anki's collection.media directory on the local filesystem.

    Probes common OS paths (including WSL2 → Windows host). Returns the
    first writable collection.media directory found, or None.
    """
    candidates: list[str] = []
    home = os.path.expanduser("~")

    if platform.system() == "Windows":
        candidates.append(os.path.join(os.getenv("APPDATA", ""), "Anki2"))
    else:
        # Native Linux
        candidates.append(os.path.join(home, ".local", "share", "Anki2"))
        # Flatpak
        candidates.append(
            os.path.join(home, ".var", "app", "net.ankiweb.Anki", "data", "Anki2")
        )
        # macOS
        candidates.append(
            os.path.join(home, "Library", "Application Support", "Anki2")
        )
        # WSL2 — Anki runs on the Windows host; search /mnt/c/Users/**
        wsl_users = glob.glob("/mnt/c/Users/*/AppData/Roaming/Anki2")
        candidates.extend(wsl_users)

    for base in candidates:
        if not os.path.isdir(base):
            continue
        for profile in os.listdir(base):
            media_dir = os.path.join(base, profile, "collection.media")
            if os.path.isdir(media_dir) and os.access(media_dir, os.W_OK):
                return media_dir
    return None


def _upload_media_direct(
    audio_uploads: list[tuple[str, bytes]], media_dir: str, verbose: bool = False
) -> int:
    """Copy audio files directly into Anki's collection.media directory.

    Much faster than AnkiConnect storeMediaFile (~0.7s for 124 files vs
    ~80s through the API) because it bypasses per-file base64 decode +
    SQLite write inside AnkiConnect.

    Returns the number of files successfully copied.
    """
    total = len(audio_uploads)
    copied = 0
    for i, (filename, data) in enumerate(audio_uploads, 1):
        dest = os.path.join(media_dir, filename)
        try:
            with open(dest, "wb") as f:
                f.write(data)
            copied += 1
            if verbose:
                print_progress(i, total, f"{filename} ({len(data)} bytes)")
            else:
                print_progress(i, total)
        except OSError:
            print()
            print(f"  ✗ {filename}: write failed")
            print_progress(i, total)
    return copied


