def _process_one_word(
    w: dict,
    book_id: str,
    no_audio: bool,
) -> tuple[dict, list[tuple[str, bytes]], str]:
    """Generate audio and build a note for a single word.

    Runs in a thread so it can be subject to a per-word timeout.

    Returns (note, audio_uploads, ipa).
    """
    word = w["word"]
    lemma = lemmatize_word(word)
    safe = safe_filename(lemma)
    ipa = w.get("ipa", "")
    audio_uploads: list[tuple[str, bytes]] = []

    if not no_audio:
        # Word audio: Claude IPA → SSML synthesis; no IPA → skip word audio
        if ipa:
            tts = edge_tts_bytes(lemma, ipa=ipa)
            if tts:
                audio_uploads.append((f"{safe}_word.mp3", tts))

        # Sentence audio: Edge TTS on cleaned sentence
        clean = re.sub(r"<[^>]+>", "", w["sentence"])
        sent_tts = edge_tts_bytes(clean)
        if sent_tts:
            audio_uploads.append((f"{safe}_sent.mp3", sent_tts))

    note = build_note_entry(w, ipa, book_id, lemma=lemma)
    return note, audio_uploads, ipa


def build_note_entry(
    word_data: dict,
    ipa: str | None,
    book_id: str,
    lemma: str | None = None,
) -> dict:
    """Build a single AnkiConnect note dict from word data.

    Audio files are referenced as [sound:filename.mp3] — the actual
    media files must be uploaded separately via store_media_file().

    If lemma is provided, it's used for the card Word field, audio filenames,
    and WordId. The original form is only preserved in the sentence.
    """
    display = (lemma or word_data["word"]).strip().lower()
    safe = safe_filename(display)
    word_audio_ref = f"[sound:{safe}_word.mp3]"
    sent_audio_ref = f"[sound:{safe}_sent.mp3]"
    word_id = f"{display}_{book_id}"

    return {
        "deckName": "",  # filled in later by caller
        "modelName": MODEL_NAME,
        "fields": {
            "WordId": word_id,
            "Word": display,
            "Sentence": word_data["sentence"],
            "IPA": ipa or word_data.get("ipa", ""),
            "DefinitionCN": word_data["definition_cn"],
            "TranslationCN": word_data["translation_cn"],
            "WordAudio": word_audio_ref,
            "SentenceAudio": sent_audio_ref,
        },
        "tags": ["weread"],
    }


def _validate_word_entries(words: list[dict]) -> list[str]:
    """Validate word entries before sync. Returns list of error messages (empty = pass).

    Checks sentence length (≤150 chars), <b> tag matches word field,
    and required fields (ipa, definition_cn, translation_cn) are non-empty.
    """
    errors = []
    for w in words:
        word = w.get("word", "")
        sentence = w.get("sentence", "")

        # 1. <b> content must match word field
        b_match = re.search(r"<b>(.*?)</b>", sentence)
        b_text = b_match.group(1) if b_match else ""
        if b_text != word:
            errors.append(f"[{word}] <b> mismatch: <b>{b_text}</b> ≠ word '{word}'")

        # 2. Sentence length ≤ 150 chars
        if len(sentence) > 150:
            errors.append(f"[{word}] sentence too long: {len(sentence)} chars (max 150)")

        # 3. Required fields non-empty
        for field in ["ipa", "definition_cn", "translation_cn"]:
            if not w.get(field):
                errors.append(f"[{word}] missing '{field}'")

    return errors


def _find_anki_media_dir() -> str | None:
    """Locate Anki's collection.media directory on the local filesystem.

    Probes common OS paths (including WSL2 → Windows host). Returns the
    first writable collection.media directory found, or None.
    """
    candidates: list[str] = []
    home = os.path.expanduser("~")

    if platform.system() == "Windows":
        candidates.append(os.path.join(os.getenv("APPDATA", ""), "Anki2"))
    else:
        # Native Linux
        candidates.append(os.path.join(home, ".local", "share", "Anki2"))
        # Flatpak
        candidates.append(
            os.path.join(home, ".var", "app", "net.ankiweb.Anki", "data", "Anki2")
        )
        # macOS
        candidates.append(
            os.path.join(home, "Library", "Application Support", "Anki2")
        )
        # WSL2 — Anki runs on the Windows host; search /mnt/c/Users/**
        wsl_users = glob.glob("/mnt/c/Users/*/AppData/Roaming/Anki2")
        candidates.extend(wsl_users)

    for base in candidates:
        if not os.path.isdir(base):
            continue
        for profile in os.listdir(base):
            media_dir = os.path.join(base, profile, "collection.media")
            if os.path.isdir(media_dir) and os.access(media_dir, os.W_OK):
                return media_dir
    return None


def _upload_media_direct(
    audio_uploads: list[tuple[str, bytes]], media_dir: str, verbose: bool = False
) -> int:
    """Copy audio files directly into Anki's collection.media directory.

    Much faster than AnkiConnect storeMediaFile (~0.7s for 124 files vs
    ~80s through the API) because it bypasses per-file base64 decode +
    SQLite write inside AnkiConnect.

    Returns the number of files successfully copied.
    """
    total = len(audio_uploads)
    copied = 0
    for i, (filename, data) in enumerate(audio_uploads, 1):
        dest = os.path.join(media_dir, filename)
        try:
            with open(dest, "wb") as f:
                f.write(data)
            copied += 1
            if verbose:
                print_progress_bar(i, total, f"{filename} ({len(data)} bytes)")
            else:
                print_progress_bar(i, total)
        except OSError:
            print()
            print(f"  ✗ {filename}: write failed")
            print_progress_bar(i, total)
    return copied


# ---------------------------------------------------------------------------
# Meta manifest card (one per book, suspended, stores excluded words)
# ---------------------------------------------------------------------------

META_MANIFEST_VERSION = 2


def _build_meta_word_id(book_id: str) -> str:
    return f"{META_WORD_PREFIX}{book_id}"


def _build_meta_note(deck_name: str, book_id: str, manifest_json: str) -> dict:
    """Build an Anki note for the meta manifest card."""
    return {
        "deckName": deck_name,
        "modelName": MODEL_NAME,
        "fields": {
            "WordId": _build_meta_word_id(book_id),
            "Word": META_WORD_PREFIX,
            "Sentence": manifest_json,
            "IPA": "",
            "DefinitionCN": "系统元数据 — 已排除单词记录",
            "TranslationCN": "此卡片已暂停，不参与每日复习",
            "WordAudio": "",
            "SentenceAudio": "",
        },
        "tags": ["weread", "meta"],
    }


def _read_meta_manifest(ac: AnkiConnect, deck_name: str, book_id: str) -> dict | None:
    """Read the meta manifest for a book from Anki. Returns None if not found."""
    meta_word_id = _build_meta_word_id(book_id)
    note_ids = ac.find_notes_by_field(deck_name, "WordId", meta_word_id)
    if not note_ids:
        return None

    try:
        info = ac.notes_info(note_ids[:1])
        if not info:
            return None
        sentence = info[0].get("fields", {}).get("Sentence", {}).get("value", "")
        if not sentence:
            return None
        manifest = json.loads(sentence)
        if manifest.get("type") != "vocab-anki-meta":
            return None
        return manifest
    except (json.JSONDecodeError, KeyError):
        return None


def _write_meta_manifest(
    ac: AnkiConnect,
    deck_name: str,
    book_id: str,
    book_title: str,
    excluded: list[dict],
) -> dict | None:
    """Create or update the meta manifest card. Returns the manifest dict.

    Suspends the card so it never appears in reviews.
    """
    meta_word_id = _build_meta_word_id(book_id)
    existing_ids = ac.find_notes_by_field(deck_name, "WordId", meta_word_id)

    # Merge with existing manifest if present
    existing_excluded: set[str] = set()
    if existing_ids:
        old = _read_meta_manifest(ac, deck_name, book_id)
        if old:
            existing_excluded = set(old.get("excluded", []))

    # Merge: union of existing + new words
    merged = existing_excluded | {entry["word"] for entry in excluded}

    manifest = {
        "type": "vocab-anki-meta",
        "version": META_MANIFEST_VERSION,
        "book_title": book_title,
        "updated_at": time.strftime("%Y-%m-%dT%H:%M:%S"),
        "excluded": sorted(merged),
    }

    manifest_json = json.dumps(manifest, ensure_ascii=False)

    if existing_ids:
        # Update existing meta note (Sentence field is the canonical storage)
        ac.update_note_fields(
            existing_ids[0],
            {"Sentence": manifest_json, "IPA": "", "WordAudio": "", "SentenceAudio": ""},
        )
    else:
        # Create new meta note
        note = _build_meta_note(deck_name, book_id, manifest_json)
        result = ac.add_notes([note])
        if result and result[0]:
            existing_ids = [result[0]]
        else:
            return manifest  # created but couldn't get note ID

    # Suspend the meta card
    if existing_ids:
        card_ids = ac.get_cards_of_notes([existing_ids[0]])
        if card_ids:
            ac.suspend_cards(card_ids)

    return manifest


def sync(
    data: dict,
    deck_name: str,
    dry_run: bool = False,
    no_audio: bool = False,
    verbose: bool = False,
    word_timeout: int = WORD_TIMEOUT,
    prefetch: bool = False,
    audio_dir: str | None = None,
    no_direct_media: bool = False,
) -> dict:
    """Run the full sync workflow. Returns a summary dict."""
    words = data["words"]
    book_title = data["book_title"]

    book_id = data.get("book_id", "")

    # 1. Connect to Anki (skip for prefetch — audio-only mode)
    ac = None
    if not prefetch:
        print(f'Connecting to AnkiConnect...')
        ac = AnkiConnect()

        # 2. Ensure deck and model exist
        ac.ensure_deck_and_model(deck_name, MODEL_NAME)
        print(f'  Deck: "{deck_name}"')
        print(f"  Model: {MODEL_NAME}")

        # 3. Read meta manifest for previously excluded words
        meta_manifest = _read_meta_manifest(ac, deck_name, book_id)
        prev_excluded: set[str] = set()
        if meta_manifest:
            prev_excluded = set(meta_manifest.get("excluded", []))
            if prev_excluded:
                print(f"  Meta manifest: {len(prev_excluded)} previously excluded words")

        # 4. Get existing WordId map from target deck
        print(f"\nQuerying existing cards in deck...")
        existing = ac.get_word_id_map(deck_name)
        print(f"  Found {len(existing)} existing cards")
    else:
        existing = {}

    # 5. Identify new words (check lemma-based WordId, fallback to original)
    new_words = []
    skipped_words = []
    for w in words:
        lemma = lemmatize_word(w["word"])
        original_id = f"{w['word'].strip().lower()}_{book_id}"
        lemma_id = f"{lemma}_{book_id}"
        if lemma_id in existing or original_id in existing:
            skipped_words.append(w)
            if verbose:
                print(f"  SKIP {w['word']} (already in deck)")
        else:
            new_words.append(w)

    print(f"\n  New words to add: {len(new_words)}")
    print(f"  Already in deck: {len(skipped_words)}")

    if not new_words:
        # Still write meta manifest if there are newly excluded words (sync mode only)
        excluded_input = data.get("excluded", [])
        if ac and excluded_input:
            try:
                _write_meta_manifest(ac, deck_name, book_id, book_title, excluded_input)
            except Exception as e:
                if verbose:
                    print(f"  (meta manifest skipped: {e})")
        print("\nDeck is up to date — nothing to add.")
        return {
            "added": 0,
            "skipped": len(skipped_words),
            "audio_uploaded": 0,
            "words_added": [],
            "words_skipped": [w["word"] for w in skipped_words],
            "prev_excluded": len(prev_excluded),
            "excluded": len(excluded_input),
        }

    if dry_run:
        print(f"\n[DRY RUN] Would add:")
        for w in new_words:
            print(f"  + {w['word']}")
        return {
            "added": len(new_words),
            "skipped": len(skipped_words),
            "audio_uploaded": 0,
            "words_added": [w["word"] for w in new_words],
            "words_skipped": [w["word"] for w in skipped_words],
        }

    # 6. Generate audio and build notes for new words (parallel)
    if prefetch:
        print(f"\nPrefetching audio for {len(new_words)} new words (parallel)...")
    else:
        print(f"\nGenerating audio for {len(new_words)} new words (parallel)...")
    notes_to_add = []
    audio_uploads: list[tuple[str, bytes]] = []
    failed_words: list[str] = []
    word_ok = 0
    sent_ok = 0
    total = len(new_words)

    if audio_dir:
        # Load pre-generated audio from disk and build notes
        manifest_path = os.path.join(audio_dir, "manifest.json")
        if not os.path.isfile(manifest_path):
            print(f"Error: manifest not found at {manifest_path}", file=sys.stderr)
            sys.exit(1)
        with open(manifest_path, "r", encoding="utf-8") as mf:
            manifest = json.load(mf)
        # Restore notes (without deckName — fill in below)
        for note in manifest["notes"]:
            note["deckName"] = deck_name
            notes_to_add.append(note)
        # Load audio files from disk
        for filename, filepath in manifest["audio_files"].items():
            if os.path.isfile(filepath):
                with open(filepath, "rb") as af:
                    audio_uploads.append((filename, af.read()))
        word_ok = manifest["stats"]["word_ok"]
        sent_ok = manifest["stats"]["sent_ok"]
        print(f"  Loaded {len(notes_to_add)} notes + {len(audio_uploads)} audio files from cache")
    else:
        # Generate audio concurrently
        max_workers = min(16, len(new_words))
        if verbose:
            print(f"  ({max_workers} workers)")

        with concurrent.futures.ThreadPoolExecutor(max_workers=max_workers) as executor:
            future_map = {
                executor.submit(_process_one_word, w, book_id, no_audio): (i, w)
                for i, w in enumerate(new_words, 1)
            }

            completed = 0
            for future in concurrent.futures.as_completed(future_map):
                i, w = future_map[future]
                word = w["word"]
                lemma = lemmatize_word(word)
                completed += 1

                try:
                    note, word_audio, final_ipa = future.result()
                    note["deckName"] = deck_name
                    notes_to_add.append(note)
                    audio_uploads.extend(word_audio)

                    has_word = any(fn.endswith("_word.mp3") for fn, _ in word_audio)
                    has_sent = any(fn.endswith("_sent.mp3") for fn, _ in word_audio)
                    if has_word:
                        word_ok += 1
                    if has_sent:
                        sent_ok += 1

                    tag = f" ({lemma})" if lemma != word.lower() else ""
                    label = f"{word}{tag}"
                    if verbose and not no_audio:
                        parts = []
                        parts.append("word✓" if has_word else "word✗")
                        parts.append("sent✓" if has_sent else "sent✗")
                        label = f"{label}  audio: {', '.join(parts)}"
                    print_progress_bar(completed, total, label)
                except Exception as e:
                    failed_words.append(word)
                    print()
                    print(f"  ✗ {word}: {e}")
                    print_progress_bar(completed, total, word)

        print()

        # If prefetch mode: save to temp dir with manifest, then exit
        if prefetch:
            prefetch_dir = tempfile.mkdtemp(prefix="vocab_audio_")
            audio_files: dict[str, str] = {}

            for filename, data in audio_uploads:
                filepath = os.path.join(prefetch_dir, filename)
                with open(filepath, "wb") as af:
                    af.write(data)
                audio_files[filename] = filepath

            # Strip deckName from notes (will be filled in at sync time)
            saved_notes = []
            for note in notes_to_add:
                saved = dict(note)
                saved.pop("deckName", None)
                saved_notes.append(saved)

            manifest = {
                "notes": saved_notes,
                "audio_files": audio_files,
                "stats": {
                    "total": total,
                    "word_ok": word_ok,
                    "sent_ok": sent_ok,
                    "failed": len(failed_words),
                },
                "failed_words": failed_words,
            }
            manifest_path = os.path.join(prefetch_dir, "manifest.json")
            with open(manifest_path, "w", encoding="utf-8") as mf:
                json.dump(manifest, mf, indent=2, ensure_ascii=False)

            print(f"\nAudio prefetch complete:")
            print(f"  Word audio:  {word_ok}/{total}")
            print(f"  Sentence:    {sent_ok}/{total}")
            if failed_words:
                print(f"  Failed:      {len(failed_words)} ({', '.join(failed_words)})")
            print(f"\nAUDIO_DIR={prefetch_dir}")
            return {
                "added": 0,
                "skipped": len(skipped_words),
                "audio_uploaded": len(audio_uploads),
                "words_added": [],
                "words_skipped": [w["word"] for w in skipped_words],
                "failed_words": failed_words,
                "prefetch_dir": prefetch_dir,
                "prefetch_stats": manifest["stats"],
            }

    # 7. Upload media files (direct filesystem copy, fallback to AnkiConnect)
    audio_count = 0
    if audio_uploads and not dry_run:
        total_media = len(audio_uploads)
        media_dir = None if no_direct_media else _find_anki_media_dir()

        if media_dir:
            # Fast path: copy files directly into collection.media (~0.7s for 124 files)
            print(f"\nCopying {total_media} media files to Anki media dir...")
            if verbose:
                print(f"  {media_dir}")
            audio_count = _upload_media_direct(audio_uploads, media_dir, verbose=verbose)
            print()
            print(f"  Copied: {audio_count}/{total_media}")
        else:
            # Slow path: upload through AnkiConnect (per-file base64 + SQLite overhead)
            batch_size = 40
            print(f"\nUploading {total_media} media files via AnkiConnect (batched ×{batch_size})...")
            for batch_start in range(0, total_media, batch_size):
                batch = audio_uploads[batch_start:batch_start + batch_size]
                try:
                    results = ac.store_media_files_batch(batch, batch_size=batch_size)
                    for i, result in enumerate(results):
                        filename, data = batch[i]
                        global_idx = batch_start + i + 1
                        if result is not None:
                            audio_count += 1
                            if verbose:
                                print_progress_bar(global_idx, total_media, f"{filename} ({len(data)} bytes)")
                            else:
                                print_progress_bar(global_idx, total_media)
                        else:
                            print()
                            print(f"  ✗ {filename}: failed (duplicate or error)")
                            print_progress_bar(global_idx, total_media)
                except AnkiConnectError as e:
                    for i, (filename, _data) in enumerate(batch):
                        global_idx = batch_start + i + 1
                        print()
                        print(f"  ✗ {filename}: {e}")
                        print_progress_bar(global_idx, total_media)
            print()
            print(f"  Uploaded: {audio_count}/{total_media}")

    # 8. Add notes
    added_count = 0
    duped_count = 0
    if not dry_run and notes_to_add:
        print(f"\nAdding {len(notes_to_add)} new cards to Anki...")
        try:
            result = ac.add_notes(notes_to_add)
            added_count = sum(1 for r in result if r is not None)
            duped_count = sum(1 for r in result if r is None)
            print(f"  Added: {added_count}")
            if duped_count:
                print(f"  Duplicates (not added): {duped_count}")
        except AnkiConnectError as e:
            errmsg = str(e)
            if "duplicate" in errmsg.lower():
                print(f"  All {len(notes_to_add)} already exist (duplicates).")
                duped_count = len(notes_to_add)
            else:
                print(f"Error adding notes: {e}")
                return {
                    "added": 0,
                    "skipped": len(skipped_words),
                    "audio_uploaded": audio_count,
                    "words_added": [],
                    "words_skipped": [w["word"] for w in skipped_words + new_words],
                    "failed_words": failed_words,
                    "error": errmsg,
                }

    # 9. Write meta manifest (cumulative excluded words record)
    excluded_input = data.get("excluded", [])
    if ac and excluded_input:
        try:
            _write_meta_manifest(ac, deck_name, book_id, book_title, excluded_input)
        except Exception as e:
            if verbose:
                print(f"  (meta manifest skipped: {e})")

    # Summary
    print(f"\n{'='*50}")
    print(f"Sync complete for \"{book_title}\"")
    print(f"  New cards added:  {added_count}")
    print(f"  Already in deck:  {len(skipped_words)}")
    print(f"  Media uploaded:   {audio_count}")
    if prev_excluded:
        print(f"  Prev. excluded:   {len(prev_excluded)} word(s)")
    if excluded_input:
        print(f"  Excluded (COCA):  {len(excluded_input)} word(s) → meta card")
    if failed_words:
        print(f"  Failed:           {len(failed_words)} word(s)")
        print(f"     ({', '.join(failed_words)})")
    print(f"{'='*50}")

    return {
        "added": added_count,
        "skipped": len(skipped_words),
        "audio_uploaded": audio_count,
        "words_added": [w["word"] for w in new_words[:added_count]],
        "words_skipped": [w["word"] for w in skipped_words],
        "failed_words": failed_words,
        "prev_excluded": len(prev_excluded),
        "excluded": len(data.get("excluded", [])),
    }


