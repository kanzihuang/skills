_CMUDICT: dict[str, list[list[str]]] | None = None  # cached cmudict


def _load_cmudict() -> dict[str, list[list[str]]]:
    """Load CMU Pronouncing Dictionary (cached)."""
    global _CMUDICT
    if _CMUDICT is not None:
        return _CMUDICT
    _CMUDICT = {}
    try:
        from pathlib import Path
        path = (
            Path(__file__).resolve().parent.parent
            / "lib" / "data" / "cmudict.dict"
        )
        if not path.exists():
            return _CMUDICT
        with open(path, encoding="utf-8", errors="replace") as fh:
            for line in fh:
                line = line.strip()
                if not line or line.startswith(";;;"):
                    continue
                parts = line.split()
                word = parts[0].lower().rstrip(")").split("(")[0]
                phones = parts[1:]
                if word not in _CMUDICT:
                    _CMUDICT[word] = []
                _CMUDICT[word].append(phones)
    except Exception:
        pass
    return _CMUDICT


# ARPAbet → IPA phoneme map
_ARPABET_TO_IPA = {
    "AA": "ɑː", "AE": "æ",  "AH": "ʌ",  "AO": "ɔː", "AW": "aʊ",
    "AY": "aɪ", "EH": "e",  "ER": "ɜːr","EY": "eɪ", "IH": "ɪ",
    "IY": "iː", "OW": "oʊ", "OY": "ɔɪ", "UH": "ʊ",  "UW": "uː",
    "B": "b",   "CH": "tʃ", "D": "d",   "DH": "ð",  "F": "f",
    "G": "ɡ",   "HH": "h",  "JH": "dʒ","K": "k",   "L": "l",
    "M": "m",   "N": "n",   "NG": "ŋ",  "P": "p",   "R": "r",
    "S": "s",   "SH": "ʃ",  "T": "t",   "TH": "θ",  "V": "v",
    "W": "w",   "Y": "j",   "Z": "z",   "ZH": "ʒ",
}


def _pick_best_pronunciation(
    entries: list[list[str]], claude_ipa: str
) -> list[str]:
    """Pick the cmudict entry closest to Claude's IPA by vowel match."""
    # Extract vowel set from Claude IPA (simplified)
    claude_vowels = set()
    for ch in claude_ipa.strip("/"):
        if ch in "iɪeæɑɔoʊuʌəɜa":
            claude_vowels.add(ch)

    def score(phones: list[str]) -> int:
        s = 0
        for p in phones:
            base = p.rstrip("012")
            mapped = _ARPABET_TO_IPA.get(base, "")
            # Count matching vowels (approximate)
            for ch in mapped:
                if ch in claude_vowels:
                    s += 1
            # Prefer exact stress match
            if p[-1].isdigit() and "ˈ" in claude_ipa and p.endswith("1"):
                s += 1
        return s

    best = max(entries, key=score)
    return best


def _cmu_ipa(word: str, claude_ipa: str = "") -> str | None:
    """Look up IPA for *word* in cmudict.  Returns None if not found.

    For multi-pronunciation words, *claude_ipa* is used as a tiebreaker
    to pick the phonetically closest entry (handles heteronyms like
    read/read, wound/wound).  Without a Claude IPA, defaults to the
    first entry.
    """
    cmu = _load_cmudict()
    w = word.lower()
    if w not in cmu:
        return None
    # Single pronunciation → use directly
    entries = cmu[w]
    if len(entries) == 1 or not claude_ipa:
        phones = entries[0]
    else:
        # Multiple → pick closest to Claude's IPA by vowel match
        phones = _pick_best_pronunciation(entries, claude_ipa)
    # Convert ARPAbet phones to IPA string
    vowel_count = sum(1 for p in phones if any(c.isdigit() for c in p))
    result: list[str] = []
    for p in phones:
        stress = ""
        if p[-1].isdigit():
            if p[-1] == "1" and vowel_count > 1:
                stress = "ˈ"
            elif p[-1] == "2" and vowel_count > 1:
                stress = "ˌ"
            p = p[:-1]
        ipa = _ARPABET_TO_IPA.get(p, p.lower())
        if stress:
            result.append(stress)
        result.append(ipa)
    return "/" + "".join(result) + "/"


def _get_spacy():
    """Load spaCy model (cached)."""
    global _SPACY_NLP
    if _SPACY_NLP is None:
        try:
            import spacy
            _SPACY_NLP = spacy.load("en_core_web_sm")
        except Exception:
            pass
    return _SPACY_NLP


def resolve_lemma(word: str, json_lemma: str) -> str:
    """Resolve the canonical lemma for a word.

    Strategy (in priority order):

    1. Explicit override: *json_lemma* differs from the surface form
       → Claude set it deliberately (e.g. derivational adj).  Trust it.

    2. Auto-correct: *json_lemma* is empty or equals the surface form
       AND the word is not a derivational adjective → run
       lemmatize_word().

    3. IRREG fallback for cases lemmatize_word() misses.

    4. Otherwise keep the provided lemma (or surface form).

    Claude should only set *lemma* for derivational adjectives /
    deliberate overrides.  Regular inflectional forms should have an
    empty *lemma* — lemmatize_word() handles those automatically.
    """
    w = word.lower()
    jl = json_lemma.strip().lower() if json_lemma else ""

    # 1. Explicit override: Claude set lemma → trust it unconditionally.
    #    The quality checklist ensures Claude sets it correctly for
    #    derivational adjectives (blundering, accomplished, etc.).
    if jl:
        return jl

    # 2. Regular -est/-er/-ier/-iest patterns (before lemmatize_word,
    #    which may over-reduce: lemminflect maps *happier* → *hap*).
    #    Only apply when the word is NOT already in COCA, preventing
    #    false reductions like beer→bee, anger→ange, sacred→sacre.
    _coca: set[str] = set()
    try:
        from lib.coca import load_coca
        _coca = load_coca()
    except ImportError:
        pass
    # -est/-iest suffixes are reliable (superlatives, near-zero false
    # positives).  -er/-ier can match nouns (beer, anger, fiber) so we
    # only apply those when the word is NOT already in COCA.
    if w not in _coca or w.endswith("est") or w.endswith("iest") or w.endswith("ier"):
        for sfx, slen in [("iest", 4), ("est", 3), ("ier", 3), ("er", 2)]:
            if w.endswith(sfx) and len(w) > slen + 1:
                stem = w[:-slen]
                if sfx.startswith("i"):
                    cand = stem + "y"          # happiest→happy, happier→happy
                else:
                    cand = stem                 # smallest→small, closer→close?
                    # Doubled consonant: biggest→big (only when the stem ends
                    # in CVC where C is the doubled consonant & not a true
                    # double-letter ending like -ll, -ss, -ff in the base).
                    if len(stem) >= 3 and stem[-1] == stem[-2] and stem[-1] not in "aeiouyls":
                        cand2 = stem[:-1]
                        cand = cand2
                    # Dropped-e: closest→close (only if stem doesn't already
                    # look like a valid word, e.g. *small* from *smallest*).
                    if cand == stem and not (
                        len(stem) >= 2
                        and stem[-1] == stem[-2]
                        and stem[-1] not in "aeiouy"
                    ):
                        cand_e = stem + "e"
                        if cand_e in _coca and cand_e != w:
                            cand = cand_e
                if cand != w and len(cand) < len(w):
                    return cand
                break

    # 3. Auto-correct inflectional forms.
    #    Apply COCA gate: only reduce if the word is NOT already in COCA,
    #    OR the reduced form is also a valid COCA entry.  This prevents
    #    false reductions like sacred→sacre where lemminflect hallucinates.
    #    (spaCy safety net in _process_one_word catches any remaining
    #    derivational adjective mis-reductions.)
    #    Note: step 3 no longer returns early — it sets *reduced* and
    #    falls through to Nation validation at step 5.
    reduced = lemmatize_word(word)
    if reduced != w and (w not in _coca or reduced in _coca):
        pass  # keep reduced, fall through to validation
    else:
        reduced = w  # reset: no valid reduction found

    # 4. lemmatize_word couldn't reduce — try lemminflect with COCA gating.
    #    Instead of returning directly, update *reduced* and fall through
    #    to the Nation validation at step 5.
    try:
        from lemminflect import getLemma

        for upos in ("VERB", "NOUN"):
            lemmas = getLemma(w, upos)
            if lemmas:
                for lemma in lemmas:
                    cand = lemma.lower()
                    if cand == w:
                        continue
                    # Path 1: word NOT in COCA → reduce if lemma IS in COCA
                    if w not in _coca and cand in _coca:
                        reduced = cand
                        break
                    # Path 2: both in COCA — accept the reduction.
                    # For VERB: accept regardless of length (had→have is
                    # longer); Nation validation at step 5 catches
                    # cross-family false positives (abode n.→abide v.).
                    # For NOUN: require shorter lemma (cats→cat safe;
                    # same-length risks: abode→abide, ran→run).
                    if w in _coca and cand in _coca:
                        if upos == "VERB" or len(cand) < len(w):
                            reduced = cand
                            break
                if reduced != w:
                    break
    except ImportError:
        pass

    # 5. Nation word family cross-validation:
    #    If the reduced lemma belongs to a DIFFERENT word family than
    #    the original word, it's a cross-family misreduction
    #    (e.g. twined→twin, but Nation says twined belongs to twine).
    #    Note: this does NOT catch intra-family reductions like
    #    blundering→blunder — Claude override + spaCy POS gate
    #    handle those.
    #    If the reduced lemma belongs to a DIFFERENT word family than
    #    the original word, it's a cross-family misreduction
    #    (e.g. twined→twin, but Nation says twined belongs to twine).
    #    Note: this does NOT catch intra-family reductions like
    #    blundering→blunder — Claude override + spaCy POS gate
    #    handle those.
    if reduced != w:
        try:
            from lib.coca import get_word_headword
            nation_head = get_word_headword(w)
            if nation_head:
                lemma_head = get_word_headword(reduced)
                if lemma_head and nation_head != lemma_head:
                    print(f"  [WARN] [{word}] lemmatize→{reduced} "
                          f"(Nation: {lemma_head} family) but Nation says "
                          f"'{w}' → {nation_head} family. "
                          f"Keeping original form.",
                          file=sys.stderr)
                    return w
        except ImportError:
            pass

    # Fall through: return reduced form (if valid), or keep original
    if reduced != w:
        return reduced
    return jl or w


def _has_be_to_pattern(doc, vbn_idx: int) -> bool:
    """Check if the VBN at vbn_idx is part of a "be VBN to <verb>" pattern.

    "be astonished to see", "be surprised to hear" etc. — emotional
    adjectives with to-infinitive complement.  spaCy's small model often
    tags these as VERB/VBN rather than ADJ, but the structural pattern
    (form of "be" before, "to" + verb after) is a reliable adjective signal.
    """
    # Check preceding tokens: a form of "be" within 3 tokens before
    be_forms = {"am", "is", "are", "was", "were", "be", "been", "being"}
    has_be = False
    for i in range(max(0, vbn_idx - 3), vbn_idx):
        if doc[i].text.lower() in be_forms:
            has_be = True
            break
    if not has_be:
        return False

    # Check following tokens: "to" within 2 tokens after, followed by verb
    for i in range(vbn_idx + 1, min(vbn_idx + 3, len(doc))):
        if doc[i].text.lower() == "to" and i + 1 < len(doc):
            if doc[i + 1].pos_ == "VERB":
                return True
            # Also catch adverbs between "to" and verb: "to properly see"
            if i + 2 < len(doc) and doc[i + 2].pos_ == "VERB":
                return True
    return False


def _process_one_word(
    w: dict,
    book_id: str,
    no_audio: bool,
) -> tuple[dict, list[tuple[str, bytes]], str]:
    """Generate audio and build a note for a single word.

    Runs in a thread so it can be subject to a per-word timeout.

    Returns (note, audio_uploads, ipa).
    """
    word = w["word"]
    json_lemma = w.get("lemma", "").strip()
    lemma = resolve_lemma(word, json_lemma)

    # Safety net: for -ed/-ing words that resolve_lemma reduced away from
    # the surface form, verify with spaCy's sentence-context parse.
    #
    # Three signals that the surface form is a derivational adjective
    # (keep it) rather than a verb inflection (reduce to stem):
    #
    #   1. Dependency-based: the word has an adjectival grammatical function
    #      (acomp/oprd/attr/amod).  This encodes "be/seem + adjective"
    #      structurally and is more reliable than POS tags alone.
    #      — catches: surprised (acomp), disappointed (oprd), distinguished (amod)
    #
    #   2. Lemma-based: spaCy returns the surface form as its own lemma
    #      (belt & suspenders for signal 1; also catches conj like embarrassed).
    #      — catches: embarrassed (conj, but lemma=embarrassed)
    #
    #   3. be-to pattern: "be VBN to <verb>" — syntactic hallmark of emotional
    #      adjectives.  Needed because spaCy sometimes misparses this as ROOT
    #      (passive) instead of acomp, e.g. "be astonished to see".
    #      — catches: astonished (ROOT, but be-to pattern matches)
    wl = word.lower()
    if lemma != wl and wl.endswith(("ed", "ing")):
        sent = w.get("sentence", "")
        if sent:
            nlp = _get_spacy()
            if nlp is not None:
                try:
                    doc = nlp(sent)
                    for token in doc:
                        if token.text.lower() == wl:
                            # Signal 1: adjectival dependency relation
                            if token.dep_ in ("acomp", "oprd", "attr", "amod"):
                                lemma = wl
                                break
                            # Signal 2: spaCy lemma == surface form
                            if token.lemma_.lower() == wl:
                                lemma = wl
                                break
                            # Signal 3: be VBN to <verb> structural pattern
                            if token.tag_ == "VBN" and _has_be_to_pattern(doc, token.i):
                                lemma = wl
                                break
                except Exception:
                    pass

    safe = safe_filename(lemma)

    # IPA: cmudict preferred.  Claude's IPA used as tiebreaker for
    # heteronyms (read/read, wound/wound) and fallback for OOV words.
    cmu_ipa = _cmu_ipa(lemma, w.get("ipa", ""))
    ipa = cmu_ipa if cmu_ipa else w.get("ipa", "")

    audio_uploads: list[tuple[str, bytes]] = []

    if not no_audio:
        # Word audio: Edge TTS on lemma (with IPA if available).
        #   Include book_id — heteronyms like "wound" have different
        #   pronunciations in different books.
        if ipa:
            tts = edge_tts_bytes(lemma, ipa=ipa)
            if tts:
                audio_uploads.append((f"{safe}_{book_id}_word.mp3", tts))

        # Sentence audio: Edge TTS on cleaned sentence.
        #   Include book_id in filename — different books have different
        #   sentences for the same lemma (e.g. "beautiful" in two books).
        clean = re.sub(r"<[^>]+>", "", w["sentence"])
        sent_tts = edge_tts_bytes(clean)
        if sent_tts:
            audio_uploads.append((f"{safe}_{book_id}_sent.mp3", sent_tts))

    note = build_note_entry(w, ipa, book_id, lemma=lemma)
    return note, audio_uploads, ipa


def build_note_entry(
    word_data: dict,
    ipa: str | None,
    book_id: str,
    lemma: str | None = None,
) -> dict:
    """Build a single AnkiConnect note dict from word data.

    Audio files are referenced as [sound:filename.mp3] — the actual
    media files must be uploaded separately via store_media_file().

    If lemma is provided, it's used for the card Word field, audio filenames,
    and WordId. The original form is only preserved in the sentence.
    """
    display = (lemma or word_data["word"]).strip().lower()
    safe = safe_filename(display)
    word_audio_ref = f"[sound:{safe}_{book_id}_word.mp3]"
    sent_audio_ref = f"[sound:{safe}_{book_id}_sent.mp3]"
    word_id = f"{display}_{book_id}"

    return {
        "deckName": "",  # filled in later by caller
        "modelName": MODEL_NAME,
        "fields": {
            "WordId": word_id,
            "Word": display,
            "Sentence": word_data["sentence"],
            "IPA": ipa or word_data.get("ipa", ""),
            "DefinitionCN": word_data["definition_cn"],
            "TranslationCN": word_data["translation_cn"],
            "WordAudio": word_audio_ref,
            "SentenceAudio": sent_audio_ref,
        },
        "tags": ["weread"],
    }


